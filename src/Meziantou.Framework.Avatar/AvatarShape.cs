namespace Meziantou.Framework;

/// <summary>
/// Indicates the avatar background shape.
/// </summary>
public enum AvatarShape
{
    /// <summary>
    /// Use a circle background.
    /// </summary>
    Round,

    /// <summary>
    /// Use a square background.
    /// </summary>
    Square,

    /// <summary>
    /// Use a square background with rounded corners.
    /// </summary>
    RoundedSquare,
}
