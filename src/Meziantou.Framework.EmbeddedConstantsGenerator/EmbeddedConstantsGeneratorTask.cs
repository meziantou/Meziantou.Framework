    internal static Result Create(GeneratorOptions options, IReadOnlyList<InputFile> files)
    {
        var errors = new List<ValidationError>();
        options.AddErrors(errors);

        var validFiles = new List<EmbeddedFile>(files.Count);
        foreach (var file in files)
        {
            var embeddedFile = EmbeddedFile.Create(file);
            errors.AddRange(embeddedFile.Errors);
            if (embeddedFile.Errors.Count == 0)
            {
                validFiles.Add(embeddedFile);
            }
        }

        var entries = CreateEntries(options, validFiles, errors);
        return new Result(options, entries, errors);
    }

    internal static string GenerateSource(GeneratorOptions options, IReadOnlyList<EmbeddedConstantEntry> entries)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.Append("namespace ").Append(options.Namespace).AppendLine(";");
        sb.AppendLine();
        sb.Append(options.ClassVisibility).Append(" static partial class ").Append(options.ClassName).AppendLine();
        sb.AppendLine("{");

        var orderedEntries = entries.OrderBy(entry => entry.BaseName, StringComparer.Ordinal).ThenBy(entry => entry.File.FullPath, StringComparer.Ordinal);
        var first = true;
        foreach (var entry in orderedEntries)
        {
            if (entry.File.Kind.HasFlag(EmbeddedConstantKind.Text))
            {
                if (!first)
                {
                    sb.AppendLine();
                }

                sb.Append("    ").Append(options.MemberVisibility).Append(" const string ").Append(entry.BaseName).Append("Text = ");
                AppendStringLiteral(sb, entry.File.Text!);
                sb.AppendLine(";");
            }

            if (entry.File.Kind.HasFlag(EmbeddedConstantKind.Binary))
            {
                if (!first)
                {
                    sb.AppendLine();
                }

                sb.Append("    ").Append(options.MemberVisibility).Append(" static global::System.ReadOnlySpan<byte> ").Append(entry.BaseName).Append("Bytes => ");
                AppendByteArray(sb, entry.File.Bytes);
                sb.AppendLine(";");
            }

            first = false;
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static List<EmbeddedConstantEntry> CreateEntries(GeneratorOptions options, List<EmbeddedFile> files, List<ValidationError> errors)
    {
        var entries = new List<EmbeddedConstantEntry>(files.Count);
        foreach (var file in files)
        {
            var baseName = !string.IsNullOrWhiteSpace(file.ExplicitName)
                ? ToPascalCaseIdentifier(file.ExplicitName)
                : ToPascalCaseIdentifier(Path.GetFileNameWithoutExtension(file.FullPath));
            entries.Add(new EmbeddedConstantEntry(file, baseName, isExplicitName: !string.IsNullOrWhiteSpace(file.ExplicitName)));
        }

        UsePathBasedNamesForImplicitDuplicates(options, entries);
        ReportDuplicateMembers(entries, errors);

        return entries;
    }

    private static void UsePathBasedNamesForImplicitDuplicates(GeneratorOptions options, List<EmbeddedConstantEntry> entries)
    {
        var duplicatedImplicitEntries = GetDuplicateMemberNames(entries)
            .SelectMany(group => group.Entries)
            .Where(entry => !entry.IsExplicitName)
            .Distinct()
            .ToArray();

        foreach (var entry in duplicatedImplicitEntries)
        {
            var relativePath = MakeRelativePath(entry.File.FullPath, options.ProjectDirectory);
            var pathWithoutExtension = Path.ChangeExtension(relativePath, extension: null);
            entry.BaseName = ToPascalCaseIdentifier(pathWithoutExtension);
        }
    }

    private static void ReportDuplicateMembers(List<EmbeddedConstantEntry> entries, List<ValidationError> errors)
    {
        foreach (var group in GetDuplicateMemberNames(entries))
        {
            errors.Add(ValidationError.DuplicateMemberName(group.MemberName));
        }
    }

    private static IEnumerable<(string MemberName, List<EmbeddedConstantEntry> Entries)> GetDuplicateMemberNames(IEnumerable<EmbeddedConstantEntry> entries)
    {
        var memberNames = new Dictionary<string, List<EmbeddedConstantEntry>>(StringComparer.Ordinal);
        foreach (var entry in entries)
        {
            foreach (var memberName in entry.GetMemberNames())
            {
                if (!memberNames.TryGetValue(memberName, out var existing))
                {
                    existing = [];
                    memberNames.Add(memberName, existing);
                }

                existing.Add(entry);
            }
        }

        foreach (var pair in memberNames)
        {
            if (pair.Value.Count > 1)
            {
                yield return (pair.Key, pair.Value);
            }
        }
    }

    private static string ToPascalCaseIdentifier(string value)
    {
        var sb = new StringBuilder();
        var newWord = true;

        foreach (var ch in value)
        {
            if (!IsIdentifierPartCharacter(ch))
            {
                newWord = true;
                continue;
            }

            if (sb.Length == 0 && !IsIdentifierStartCharacter(ch))
            {
                sb.Append('_');
            }

            sb.Append(newWord ? char.ToUpperInvariant(ch) : ch);
            newWord = false;
        }

        if (sb.Length == 0)
            return "File";

        return sb.ToString();
    }

    private static bool IsValidNamespace(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
            return false;

        foreach (var part in value.Split('.'))
        {
            if (!IsValidIdentifier(part))
                return false;
        }

        return true;
    }

    private static bool IsValidIdentifier(string value)
    {
        if (string.IsNullOrWhiteSpace(value) || !IsIdentifierStartCharacter(value[0]) || CSharpKeywords.Contains(value))
            return false;

        for (var i = 1; i < value.Length; i++)
        {
            if (!IsIdentifierPartCharacter(value[i]))
                return false;
        }

        return true;
    }

    private static bool IsIdentifierStartCharacter(char ch)
    {
        if (ch == '_')
            return true;

        return char.GetUnicodeCategory(ch) is UnicodeCategory.UppercaseLetter or UnicodeCategory.LowercaseLetter or UnicodeCategory.TitlecaseLetter or UnicodeCategory.ModifierLetter or UnicodeCategory.OtherLetter or UnicodeCategory.LetterNumber;
    }

    private static bool IsIdentifierPartCharacter(char ch)
    {
        if (IsIdentifierStartCharacter(ch))
            return true;

        return char.GetUnicodeCategory(ch) is UnicodeCategory.DecimalDigitNumber or UnicodeCategory.ConnectorPunctuation or UnicodeCategory.NonSpacingMark or UnicodeCategory.SpacingCombiningMark or UnicodeCategory.Format;
    }

    private static bool IsValidVisibility(string value)
    {
        return string.Equals(value, "internal", StringComparison.Ordinal) || string.Equals(value, "public", StringComparison.Ordinal);
    }

    private static bool TryParseKind(string value, out EmbeddedConstantKind kind)
    {
        if (string.Equals(value, "Text", StringComparison.OrdinalIgnoreCase))
        {
            kind = EmbeddedConstantKind.Text;
            return true;
        }

        if (string.Equals(value, "Binary", StringComparison.OrdinalIgnoreCase))
        {
            kind = EmbeddedConstantKind.Binary;
            return true;
        }

        if (string.Equals(value, "Both", StringComparison.OrdinalIgnoreCase))
        {
            kind = EmbeddedConstantKind.Both;
            return true;
        }

        kind = EmbeddedConstantKind.None;
        return false;
    }

    private static byte[] RemoveUtf8Bom(byte[] bytes)
    {
        if (bytes.Length >= 3 && bytes is [0xEF, 0xBB, 0xBF, ..])
        {
            var result = new byte[bytes.Length - 3];
            Array.Copy(bytes, 3, result, 0, result.Length);
            return result;
        }

        return bytes;
    }

    private static string MakeRelativePath(string path, string? basePath)
    {
        if (string.IsNullOrWhiteSpace(basePath))
            return path;

        try
        {
            var fullBasePath = Path.GetFullPath(basePath).TrimEnd(Path.DirectorySeparatorChar, Path.AltDirectorySeparatorChar) + Path.DirectorySeparatorChar;
            var fullPath = Path.GetFullPath(path);
            if (fullPath.StartsWith(fullBasePath, StringComparison.OrdinalIgnoreCase))
            {
                return fullPath.Substring(fullBasePath.Length);
            }
        }
        catch (Exception)
        {
            return path;
        }

        return path;
    }

    private static void AppendStringLiteral(StringBuilder sb, string value)
    {
        sb.Append('"');
        foreach (var ch in value)
        {
            switch (ch)
            {
                case '\\':
                    sb.Append(@"\\");
                    break;
                case '"':
                    sb.Append("\\\"");
                    break;
                case '\0':
                    sb.Append(@"\0");
                    break;
                case '\a':
                    sb.Append(@"\a");
                    break;
                case '\b':
                    sb.Append(@"\b");
                    break;
                case '\f':
                    sb.Append(@"\f");
                    break;
                case '\n':
                    sb.Append(@"\n");
                    break;
                case '\r':
                    sb.Append(@"\r");
                    break;
                case '\t':
                    sb.Append(@"\t");
                    break;
                case '\v':
                    sb.Append(@"\v");
                    break;
                default:
                    if (char.IsControl(ch) || char.GetUnicodeCategory(ch) is UnicodeCategory.LineSeparator or UnicodeCategory.ParagraphSeparator)
                    {
                        sb.Append(@"\u");
                        sb.Append(((int)ch).ToString("X4", CultureInfo.InvariantCulture));
                    }
                    else
                    {
                        sb.Append(ch);
                    }

                    break;
            }
        }

        sb.Append('"');
    }

    private static void AppendByteArray(StringBuilder sb, byte[] bytes)
    {
        if (bytes.Length == 0)
        {
            sb.Append("global::System.ReadOnlySpan<byte>.Empty");
            return;
        }

        sb.AppendLine("new byte[]");
        sb.AppendLine("    {");
        for (var i = 0; i < bytes.Length; i++)
        {
            if (i % 16 == 0)
            {
                sb.Append("        ");
            }

            sb.Append("0x");
            sb.Append(bytes[i].ToString("X2", CultureInfo.InvariantCulture));
            if (i != bytes.Length - 1)
            {
                sb.Append(", ");
            }

            if (i % 16 == 15 || i == bytes.Length - 1)
            {
                sb.AppendLine();
            }
        }

        sb.Append("    }");
    }

    internal sealed class Result
    {
        public Result(GeneratorOptions options, List<EmbeddedConstantEntry> entries, List<ValidationError> errors)
        {
            Options = options;
            Entries = entries;
            Errors = errors;
        }

        public GeneratorOptions Options { get; }
        public IReadOnlyList<EmbeddedConstantEntry> Entries { get; }
        public IReadOnlyList<ValidationError> Errors { get; }
        public bool HasErrors => Errors.Count != 0;
    }

    internal sealed class GeneratorOptions
    {
        public GeneratorOptions(string ns, string className, string classVisibility, string memberVisibility, string? projectDirectory)
        {
            Namespace = ns;
            ClassName = className;
            ClassVisibility = classVisibility.ToLowerInvariant();
            MemberVisibility = memberVisibility.ToLowerInvariant();
            ProjectDirectory = projectDirectory;
        }

        public string Namespace { get; }
        public string ClassName { get; }
        public string ClassVisibility { get; }
        public string MemberVisibility { get; }
        public string? ProjectDirectory { get; }

        public void AddErrors(List<ValidationError> errors)
        {
            if (!IsValidNamespace(Namespace))
            {
                errors.Add(ValidationError.InvalidNamespace(Namespace));
            }

            if (!IsValidIdentifier(ClassName))
            {
                errors.Add(ValidationError.InvalidClassName(ClassName));
            }

            if (!IsValidVisibility(ClassVisibility))
            {
                errors.Add(ValidationError.InvalidVisibility(ClassVisibility));
            }

            if (!IsValidVisibility(MemberVisibility))
            {
                errors.Add(ValidationError.InvalidVisibility(MemberVisibility));
            }
        }
    }

    internal sealed class InputFile
    {
        public InputFile(string path, string? kind, string? explicitName, string? projectDirectory)
        {
            OriginalPath = path;
            FullPath = GetFullPath(path, projectDirectory);
            Kind = kind;
            ExplicitName = explicitName;
        }

        public string OriginalPath { get; }
        public string FullPath { get; }
        public string? Kind { get; }
        public string? ExplicitName { get; }

        private static string GetFullPath(string path, string? projectDirectory)
        {
            if (Path.IsPathRooted(path) || string.IsNullOrWhiteSpace(projectDirectory))
                return path;

            return Path.GetFullPath(Path.Combine(projectDirectory, path));
        }
    }

    internal sealed class EmbeddedFile
    {
        private EmbeddedFile(string fullPath, EmbeddedConstantKind kind, string? explicitName, string? text, byte[] bytes, List<ValidationError> errors)
        {
            FullPath = fullPath;
            Kind = kind;
            ExplicitName = explicitName;
            Text = text;
            Bytes = bytes;
            Errors = errors;
        }

        public string FullPath { get; }
        public EmbeddedConstantKind Kind { get; }
        public string? ExplicitName { get; }
        public string? Text { get; }
        public byte[] Bytes { get; }
        public IReadOnlyList<ValidationError> Errors { get; }

        public static EmbeddedFile Create(InputFile file)
        {
            var errors = new List<ValidationError>();
            if (string.IsNullOrWhiteSpace(file.Kind))
            {
                errors.Add(ValidationError.MissingKind(file.FullPath));
                return new EmbeddedFile(file.FullPath, EmbeddedConstantKind.None, file.ExplicitName, text: null, Array.Empty<byte>(), errors);
            }

            if (!TryParseKind(file.Kind, out var kind))
            {
                errors.Add(ValidationError.UnsupportedKind(file.FullPath, file.Kind));
                return new EmbeddedFile(file.FullPath, EmbeddedConstantKind.None, file.ExplicitName, text: null, Array.Empty<byte>(), errors);
            }

            byte[] bytes;
            try
            {
                bytes = File.ReadAllBytes(file.FullPath);
            }
            catch (Exception ex)
            {
                errors.Add(ValidationError.CannotReadFile(file.FullPath, ex.Message));
                return new EmbeddedFile(file.FullPath, kind, file.ExplicitName, text: null, Array.Empty<byte>(), errors);
            }

            if (kind == EmbeddedConstantKind.Binary)
            {
                return new EmbeddedFile(file.FullPath, kind, file.ExplicitName, text: null, bytes, errors);
            }

            var textBytes = RemoveUtf8Bom(bytes);
            if (textBytes.Length > MaxTextFileBytes)
            {
                errors.Add(ValidationError.TextFileTooLarge(file.FullPath, MaxTextFileBytes));
                return new EmbeddedFile(file.FullPath, kind, file.ExplicitName, text: null, Array.Empty<byte>(), errors);
            }

            try
            {
                var content = Utf8NoBomThrowOnInvalidBytes.GetString(textBytes, 0, textBytes.Length);
                return new EmbeddedFile(file.FullPath, kind, file.ExplicitName, content, textBytes, errors);
            }
            catch (DecoderFallbackException)
            {
                errors.Add(ValidationError.InvalidTextEncoding(file.FullPath));
                return new EmbeddedFile(file.FullPath, kind, file.ExplicitName, text: null, Array.Empty<byte>(), errors);
            }
        }
    }

    internal sealed class EmbeddedConstantEntry
    {
        public EmbeddedConstantEntry(EmbeddedFile file, string baseName, bool isExplicitName)
        {
            File = file;
            BaseName = baseName;
            IsExplicitName = isExplicitName;
        }

        public EmbeddedFile File { get; }
        public string BaseName { get; set; }
        public bool IsExplicitName { get; }

        public IEnumerable<string> GetMemberNames()
        {
            if (File.Kind is EmbeddedConstantKind.Text or EmbeddedConstantKind.Both)
            {
                yield return BaseName + "Text";
            }

            if (File.Kind is EmbeddedConstantKind.Binary or EmbeddedConstantKind.Both)
            {
                yield return BaseName + "Bytes";
            }
        }
    }

    internal sealed class ValidationError
    {
        private ValidationError(string code, string message, string? filePath)
        {
            Code = code;
            Message = message;
            FilePath = filePath;
        }

        public string Code { get; }
        public string Message { get; }
        public string? FilePath { get; }

        public static ValidationError InvalidNamespace(string ns)
        {
            return new ValidationError("MFECG0001", string.Create(CultureInfo.InvariantCulture, $"Embedded constants namespace '{ns}' is missing or invalid"), filePath: null);
        }

        public static ValidationError InvalidClassName(string className)
        {
            return new ValidationError("MFECG0002", string.Create(CultureInfo.InvariantCulture, $"Embedded constants class name '{className}' is missing or invalid"), filePath: null);
        }

        public static ValidationError MissingKind(string filePath)
        {
            return new ValidationError("MFECG0003", "Embedded constant file must set Kind to Text, Binary, or Both", filePath);
        }

        public static ValidationError UnsupportedKind(string filePath, string kind)
        {
            return new ValidationError("MFECG0004", string.Create(CultureInfo.InvariantCulture, $"Embedded constant file has unsupported Kind value '{kind}'"), filePath);
        }

        public static ValidationError DuplicateMemberName(string memberName)
        {
            return new ValidationError("MFECG0005", string.Create(CultureInfo.InvariantCulture, $"Generated member name '{memberName}' is used by multiple embedded constant files"), filePath: null);
        }

        public static ValidationError CannotReadFile(string filePath, string message)
        {
            return new ValidationError("MFECG0006", string.Create(CultureInfo.InvariantCulture, $"Embedded constant file cannot be read: {message}"), filePath);
        }

        public static ValidationError InvalidTextEncoding(string filePath)
        {
            return new ValidationError("MFECG0007", "Embedded constant text file must be valid UTF-8 text", filePath);
        }

        public static ValidationError TextFileTooLarge(string filePath, int maxSize)
        {
            return new ValidationError("MFECG0008", string.Create(CultureInfo.InvariantCulture, $"Embedded constant text file is too large to embed as a const string. The maximum supported text file size is {maxSize} bytes."), filePath);
        }

        public static ValidationError InvalidVisibility(string visibility)
        {
            return new ValidationError("MFECG0009", string.Create(CultureInfo.InvariantCulture, $"Embedded constants visibility '{visibility}' is invalid. Supported values are 'internal' and 'public'."), filePath: null);
        }
    }

    [Flags]
    internal enum EmbeddedConstantKind
    {
        None = 0,
        Text = 1,
        Binary = 2,
        Both = Text | Binary,
    }
}
