using System.Collections.Immutable;
using System.Globalization;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Meziantou.Framework.FastEnumGenerator;

[Generator]
public sealed class FastEnumSourceGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(static context =>
        {
            context.AddSource("Microsoft.CodeAnalysis.EmbeddedAttribute.g.cs", SourceText.From("""
                // <auto-generated/>
                #pragma warning disable
                namespace Microsoft.CodeAnalysis
                {
                    [global::System.Runtime.CompilerServices.CompilerGenerated]
                    [global::System.CodeDom.Compiler.GeneratedCode("Meziantou.Framework.FastEnumGenerator", "1.0.0.0")]
                    internal sealed partial class EmbeddedAttribute : global::System.Attribute
                    {
                    }
                }
                """, encoding: Encoding.UTF8));

            context.AddSource("Meziantou.Framework.Annotations.FastEnumAttribute.g.cs", SourceText.From("""
                // <auto-generated/>
                #pragma warning disable
                namespace Meziantou.Framework.Annotations
                {
                    [global::Microsoft.CodeAnalysis.Embedded]
                    [global::System.Runtime.CompilerServices.CompilerGenerated]
                    [global::System.CodeDom.Compiler.GeneratedCode("Meziantou.Framework.FastEnumGenerator", "1.0.0.0")]
                    [global::System.Diagnostics.Conditional("FastEnum_Attributes")]
                    [global::System.AttributeUsage(global::System.AttributeTargets.Assembly, AllowMultiple = true)]
                    internal sealed class FastEnumAttribute : global::System.Attribute
                    {
                        public FastEnumAttribute(global::System.Type enumType)
                        {
                            EnumType = enumType;
                        }

                        public bool IsPublic { get; set; } = true;

                        public string? ExtensionMethodNamespace { get; set; }

                        public global::System.Type EnumType { get; }
                    }

                }
                """, encoding: Encoding.UTF8));
        });

        var enums = context.SyntaxProvider.CreateSyntaxProvider(
            predicate: static (syntax, _) => syntax.IsKind(SyntaxKind.Attribute),
            transform: static (ctx, cancellationToken) => GetSemanticTargetForGeneration(ctx, cancellationToken))
            .Where(static item => item is not null);

        var enumToProcess = enums.Collect();
        var supportsExtensionMembers = context.CompilationProvider.Select(static (compilation, _) => SupportsExtensionMembers(compilation));
        var generationInput = enumToProcess.Combine(supportsExtensionMembers);

        context.RegisterSourceOutput(generationInput, static (spc, source) => GenerateCode(spc, source.Left, source.Right));
    }

    private static EnumToProcess? GetSemanticTargetForGeneration(GeneratorSyntaxContext ctx, CancellationToken cancellationToken)
    {
        var compilation = ctx.SemanticModel.Compilation;
        var fastEnumAttributeSymbol = compilation.GetTypeByMetadataName("Meziantou.Framework.Annotations.FastEnumAttribute");
        var flagsAttributeSymbol = compilation.GetTypeByMetadataName("System.FlagsAttribute");
        if (fastEnumAttributeSymbol is null)
            return null;

        var attributeSyntax = (AttributeSyntax)ctx.Node;
        foreach (var attr in compilation.Assembly.GetAttributes())
        {
            if (attr.ApplicationSyntaxReference?.GetSyntax(cancellationToken) != attributeSyntax)
                continue;

            var isFastEnumAttribute = SymbolEqualityComparer.Default.Equals(attr.AttributeClass, fastEnumAttributeSymbol);
            if (!isFastEnumAttribute)
                continue;

            if (attr.ConstructorArguments.Length != 1)
                continue;

            if (attr.ConstructorArguments[0].Value is not ITypeSymbol enumTypeSymbol || enumTypeSymbol is not INamedTypeSymbol enumType)
                continue;

            if (enumType.TypeKind != TypeKind.Enum)
                continue;

            var members = GetMembers(enumType);
            if (members.Length == 0)
                continue;

            var isPublic = GetIsPublic(attr, enumType);
            var extensionNamespace = GetExtensionNamespace(attr);
            var isFlags = flagsAttributeSymbol is not null &&
                          enumType.GetAttributes().Any(attribute => SymbolEqualityComparer.Default.Equals(attribute.AttributeClass, flagsAttributeSymbol));
            var isZeroBasedConsecutive = !isFlags && IsZeroBasedConsecutive(members);
            var enumUnderlyingType = enumType.EnumUnderlyingType;
            var underlyingType = enumUnderlyingType?.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat) ?? "global::System.Int32";
            var isUnderlyingTypeSigned = enumUnderlyingType is not null && IsSignedIntegralType(enumUnderlyingType.SpecialType);

            return new EnumToProcess(enumType, members, isPublic, extensionNamespace, isFlags, isZeroBasedConsecutive, underlyingType, isUnderlyingTypeSigned);
        }

        return null;
    }

    private static bool SupportsExtensionMembers(Compilation compilation)
    {
        if (compilation is not CSharpCompilation csharpCompilation)
            return false;

        var firstSyntaxTree = csharpCompilation.SyntaxTrees.FirstOrDefault();
        if (firstSyntaxTree is null)
            return false;

        if (firstSyntaxTree.Options is not CSharpParseOptions parseOptions)
            return false;

        return parseOptions.LanguageVersion >= (LanguageVersion)1400;
    }

    private static ImmutableArray<EnumMemberToProcess> GetMembers(INamedTypeSymbol enumType)
    {
        var members = ImmutableArray.CreateBuilder<EnumMemberToProcess>();
        foreach (var member in enumType.GetMembers())
        {
            if (member is not IFieldSymbol field || field.ConstantValue is null)
                continue;

            var metadataName = GetMetadataName(field);
            members.Add(new EnumMemberToProcess(field.Name, field.ConstantValue, ConvertToUInt64(field.ConstantValue), metadataName));
        }

        return members.ToImmutable();
    }

    private static bool GetIsPublic(AttributeData attr, INamedTypeSymbol enumType)
    {
        var isPublic = IsVisibleOutsideOfAssembly(enumType);
        foreach (var argument in attr.NamedArguments)
        {
            if (argument is { Key: "IsPublic", Value.Value: bool value })
                return isPublic && value;
        }

        return isPublic;
    }

    private static string? GetExtensionNamespace(AttributeData attr)
    {
        foreach (var argument in attr.NamedArguments)
        {
            if (argument is { Key: "ExtensionMethodNamespace", Value.Value: string value })
                return value;
        }

        return null;
    }

    private static string? GetMetadataName(IFieldSymbol field)
    {
        foreach (var attribute in field.GetAttributes())
        {
            if (!string.Equals(attribute.AttributeClass?.ToDisplayString(), "System.ComponentModel.DataAnnotations.DisplayAttribute", StringComparison.Ordinal))
                continue;

            foreach (var namedArgument in attribute.NamedArguments)
            {
                if (namedArgument is { Key: "Name", Value.Value: string value } && !string.IsNullOrEmpty(value))
                    return value;
            }
        }

        foreach (var attribute in field.GetAttributes())
        {
            if (!string.Equals(attribute.AttributeClass?.ToDisplayString(), "System.ComponentModel.DisplayNameAttribute", StringComparison.Ordinal))
                continue;

            if (attribute.ConstructorArguments is [{ Value: string value }] && !string.IsNullOrEmpty(value))
                return value;
        }

        foreach (var attribute in field.GetAttributes())
        {
            if (!string.Equals(attribute.AttributeClass?.ToDisplayString(), "System.Runtime.Serialization.EnumMemberAttribute", StringComparison.Ordinal))
                continue;

            foreach (var namedArgument in attribute.NamedArguments)
            {
                if (namedArgument is { Key: "Value", Value.Value: string value } && !string.IsNullOrEmpty(value))
                    return value;
            }
        }

        return null;
    }

    private static bool IsSignedIntegralType(SpecialType specialType)
    {
        return specialType is SpecialType.System_SByte or SpecialType.System_Int16 or SpecialType.System_Int32 or SpecialType.System_Int64;
    }

    private static ulong ConvertToUInt64(object value)
    {
        return value switch
        {
            byte byteValue => byteValue,
            sbyte sbyteValue => unchecked((ulong)sbyteValue),
            short shortValue => unchecked((ulong)shortValue),
            ushort ushortValue => ushortValue,
            int intValue => unchecked((ulong)intValue),
            uint uintValue => uintValue,
            long longValue => unchecked((ulong)longValue),
            ulong ulongValue => ulongValue,
            _ => 0,
        };
    }

    private static bool IsZeroBasedConsecutive(ImmutableArray<EnumMemberToProcess> members)
    {
        var values = new HashSet<ulong>();
        foreach (var member in members)
        {
            _ = values.Add(member.UInt64Value);
        }

        if (!values.Contains(0))
            return false;

        var count = values.Count;
        for (ulong i = 0; i < (ulong)count; i++)
        {
            if (!values.Contains(i))
                return false;
        }

        return true;
    }

    private static void GenerateCode(SourceProductionContext context, ImmutableArray<EnumToProcess?> enumToProcess, bool supportsExtensionMembers)
    {
        var enums = enumToProcess.Where(static item => item is not null).Cast<EnumToProcess>().ToImmutableArray();
        if (enums.Length == 0)
            return;

        var code = GenerateCode(enums, supportsExtensionMembers);
        context.AddSource("FastEnumExtensions.g.cs", SourceText.From(code, Encoding.UTF8));
    }

    private static string GenerateCode(ImmutableArray<EnumToProcess> enums, bool supportsExtensionMembers)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#pragma warning disable");

        var enumIndex = 0;
        foreach (var enumerationGroup in enums.GroupBy(static item => item.FullNamespace, StringComparer.Ordinal).OrderBy(static group => group.Key, StringComparer.Ordinal))
        {
            var typeVisibility = enumerationGroup.Any(static item => item.IsPublic) ? "public" : "internal";
            foreach (var enumeration in enumerationGroup.OrderBy(static item => item.FullCsharpName, StringComparer.Ordinal))
            {
                AppendEnumCode(sb, enumeration, typeVisibility, supportsExtensionMembers, enumIndex);
                enumIndex++;
            }
        }

        return sb.ToString();
    }

    private static void AppendEnumCode(StringBuilder sb, EnumToProcess enumeration, string typeVisibility, bool supportsExtensionMembers, int enumIndex)
    {
        var methodVisibility = enumeration.IsPublic ? "public" : "internal";
        var enumTypeName = "global::" + enumeration.FullCsharpName;

        var uniqueMembers = GetUniqueMembers(enumeration.Members);
        var parseTokens = GetParseTokens(enumeration.Members);
        var hasMetadata = enumeration.Members.Any(static item => item.MetadataName is not null);
        var denseMembers = enumeration.IsZeroBasedConsecutive ? uniqueMembers.OrderBy(static item => item.UInt64Value).ToList() : [];
        var flagMembers = enumeration.IsFlags
            ? uniqueMembers.Where(static item => item.UInt64Value != 0 && IsPowerOfTwo(item.UInt64Value)).OrderByDescending(static item => item.UInt64Value).ToList()
            : [];
        var className = "FastEnumExtensions_" + enumIndex.ToString(CultureInfo.InvariantCulture);

        if (!string.IsNullOrEmpty(enumeration.FullNamespace))
        {
            sb.Append("namespace ").Append(enumeration.FullNamespace).AppendLine();
            sb.AppendLine("{");
        }

        sb.Append(typeVisibility).Append(" static class ").Append(className).AppendLine();
        sb.AppendLine("{");

        AppendMemberArrays(sb, enumeration, enumTypeName, enumIndex, parseTokens);
        if (denseMembers.Count > 0)
        {
            AppendDenseArrays(sb, denseMembers, enumTypeName, enumIndex);
        }

        if (hasMetadata && flagMembers.Count > 0)
        {
            AppendFlagsArrays(sb, flagMembers, enumIndex);
        }

        sb.Append("    ").Append(methodVisibility).Append(" static string ToStringFast(this ").Append(enumTypeName).AppendLine(" value)");
        sb.AppendLine("    {");
        sb.Append("        return GetNameCore_").Append(enumIndex).AppendLine("(value, useMetadata: false);");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    ").Append(methodVisibility).Append(" static string ToStringFast(this ").Append(enumTypeName).AppendLine(" value, bool useMetadata)");
        sb.AppendLine("    {");
        sb.Append("        return GetNameCore_").Append(enumIndex).AppendLine("(value, useMetadata);");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    ").Append(methodVisibility).Append(" static bool HasFlag(this ").Append(enumTypeName).Append(" instance, ").Append(enumTypeName).AppendLine(" flag)");
        sb.AppendLine("    {");
        sb.AppendLine("        return (instance & flag) == flag;");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    ").Append(methodVisibility).Append(" static string GetName(this ").Append(enumTypeName).AppendLine(" instance)");
        sb.AppendLine("    {");
        sb.Append("        return GetNameCore_").Append(enumIndex).AppendLine("(instance, useMetadata: false);");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    private static string GetNameCore_").Append(enumIndex).Append('(').Append(enumTypeName).AppendLine(" value, bool useMetadata)");
        sb.AppendLine("    {");
        if (denseMembers.Count > 0)
        {
            sb.Append("        if (TryGetDenseIndex_").Append(enumIndex).AppendLine("(value, out var index))");
            sb.AppendLine("        {");
            sb.Append("            return useMetadata ? s_definedMetadataNames_").Append(enumIndex).Append("[index] : s_definedNames_").Append(enumIndex).AppendLine("[index];");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        return value.ToString();");
        }
        else
        {
            sb.AppendLine("        switch (value)");
            sb.AppendLine("        {");
            foreach (var member in uniqueMembers)
            {
                sb.Append("            case ").Append(enumTypeName).Append('.').Append(member.Name).AppendLine(":");
                sb.Append("                return useMetadata ? ").Append(ToLiteral(member.MetadataName ?? member.Name)).Append(" : nameof(").Append(enumTypeName).Append('.').Append(member.Name).AppendLine(");");
            }

            sb.AppendLine("        }");
            sb.AppendLine();
            if (hasMetadata && flagMembers.Count > 0)
            {
                sb.AppendLine("        if (useMetadata)");
                sb.AppendLine("        {");
                sb.Append("            return FormatFlagsName_").Append(enumIndex).AppendLine("(value);");
                sb.AppendLine("        }");
                sb.AppendLine();
            }

            sb.AppendLine("        return value.ToString();");
        }

        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    private static bool IsDefined_").Append(enumIndex).Append('(').Append(enumTypeName).AppendLine(" value)");
        sb.AppendLine("    {");
        if (denseMembers.Count > 0)
        {
            sb.Append("        return TryGetDenseIndex_").Append(enumIndex).AppendLine("(value, out _);");
        }
        else
        {
            sb.AppendLine("        return value switch");
            sb.AppendLine("        {");
            foreach (var member in uniqueMembers)
            {
                sb.Append("            ").Append(enumTypeName).Append('.').Append(member.Name).AppendLine(" => true,");
            }

            sb.AppendLine("            _ => false,");
            sb.AppendLine("        };");
        }

        sb.AppendLine("    }");
        sb.AppendLine();

        if (denseMembers.Count > 0)
        {
            sb.Append("    private static bool TryGetDenseIndex_").Append(enumIndex).Append('(').Append(enumTypeName).AppendLine(" value, out int index)");
            sb.AppendLine("    {");
            sb.Append("        var numericValue = unchecked((").Append(enumeration.UnderlyingTypeName).AppendLine(")value);");
            if (enumeration.IsUnderlyingTypeSigned)
            {
                sb.Append("        if (numericValue < 0 || numericValue >= unchecked((").Append(enumeration.UnderlyingTypeName).Append(")s_definedNames_").Append(enumIndex).AppendLine(".Length))");
            }
            else
            {
                sb.Append("        if (numericValue >= unchecked((").Append(enumeration.UnderlyingTypeName).Append(")s_definedNames_").Append(enumIndex).AppendLine(".Length))");
            }

            sb.AppendLine("        {");
            sb.AppendLine("            index = default;");
            sb.AppendLine("            return false;");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        index = unchecked((int)numericValue);");
            sb.AppendLine("        return true;");
            sb.AppendLine("    }");
            sb.AppendLine();
        }

        sb.Append("    private static ulong ToUInt64_").Append(enumIndex).Append('(').Append(enumTypeName).AppendLine(" value)");
        sb.AppendLine("    {");
        sb.Append("        return unchecked((ulong)((").Append(enumeration.UnderlyingTypeName).AppendLine(")value));");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    private static ").Append(enumTypeName).Append(" FromUInt64_").Append(enumIndex).AppendLine("(ulong value)");
        sb.AppendLine("    {");
        sb.Append("        return unchecked((").Append(enumTypeName).Append(")((").Append(enumeration.UnderlyingTypeName).AppendLine(")value));");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.Append("    private static bool EqualsToken_").Append(enumIndex).AppendLine("(global::System.ReadOnlySpan<char> value, string token, bool ignoreCase)");
        sb.AppendLine("    {");
        sb.AppendLine("        var comparison = ignoreCase ? global::System.StringComparison.OrdinalIgnoreCase : global::System.StringComparison.Ordinal;");
        sb.AppendLine("        return global::System.MemoryExtensions.Equals(value, (global::System.ReadOnlySpan<char>)token, comparison);");
        sb.AppendLine("    }");
        sb.AppendLine();

        if (hasMetadata && flagMembers.Count > 0)
        {
            AppendFormatFlagsName(sb, enumTypeName, enumIndex);
        }

        if (supportsExtensionMembers)
        {
            AppendTryParseHelpers(sb, enumTypeName, enumeration, enumIndex);
            AppendExtensionMembers(sb, enumTypeName, methodVisibility, enumeration, enumIndex);
        }

        sb.AppendLine("}");
        if (!string.IsNullOrEmpty(enumeration.FullNamespace))
        {
            sb.AppendLine("}");
        }
    }

    private static void AppendMemberArrays(StringBuilder sb, EnumToProcess enumeration, string enumTypeName, int enumIndex, List<ParseTokenToProcess> parseTokens)
    {
        sb.Append("    private static readonly string[] s_names_").Append(enumIndex).Append(" = new string[] { ");
        AppendCommaSeparated(sb, enumeration.Members.Select(static member => ToLiteral(member.Name)));
        sb.AppendLine(" };");

        sb.Append("    private static readonly string[] s_metadataNames_").Append(enumIndex).Append(" = new string[] { ");
        AppendCommaSeparated(sb, enumeration.Members.Select(static member => ToLiteral(member.MetadataName ?? member.Name)));
        sb.AppendLine(" };");

        sb.Append("    private static readonly ").Append(enumTypeName).Append("[] s_values_").Append(enumIndex).Append(" = new ").Append(enumTypeName).Append("[] { ");
        AppendCommaSeparated(sb, enumeration.Members.Select(member => enumTypeName + "." + member.Name));
        sb.AppendLine(" };");

        sb.Append("    private static readonly string[] s_parseTokens_").Append(enumIndex).Append(" = new string[] { ");
        AppendCommaSeparated(sb, parseTokens.Select(static token => ToLiteral(token.Token)));
        sb.AppendLine(" };");

        sb.Append("    private static readonly ").Append(enumTypeName).Append("[] s_parseValues_").Append(enumIndex).Append(" = new ").Append(enumTypeName).Append("[] { ");
        AppendCommaSeparated(sb, parseTokens.Select(token => enumTypeName + "." + token.MemberName));
        sb.AppendLine(" };");

        sb.Append("    private static readonly bool[] s_parseTokenIsMetadata_").Append(enumIndex).Append(" = new bool[] { ");
        AppendCommaSeparated(sb, parseTokens.Select(static token => token.IsMetadata ? "true" : "false"));
        sb.AppendLine(" };");
        sb.AppendLine();
    }

    private static void AppendDenseArrays(StringBuilder sb, List<EnumMemberToProcess> denseMembers, string enumTypeName, int enumIndex)
    {
        sb.Append("    private static readonly string[] s_definedNames_").Append(enumIndex).Append(" = new string[] { ");
        AppendCommaSeparated(sb, denseMembers.Select(member => "nameof(" + enumTypeName + "." + member.Name + ")"));
        sb.AppendLine(" };");

        sb.Append("    private static readonly string[] s_definedMetadataNames_").Append(enumIndex).Append(" = new string[] { ");
        AppendCommaSeparated(sb, denseMembers.Select(member => ToLiteral(member.MetadataName ?? member.Name)));
        sb.AppendLine(" };");
        sb.AppendLine();
    }

    private static void AppendFlagsArrays(StringBuilder sb, List<EnumMemberToProcess> flagMembers, int enumIndex)
    {
        sb.Append("    private static readonly ulong[] s_flagValues_").Append(enumIndex).Append(" = new ulong[] { ");
        AppendCommaSeparated(sb, flagMembers.Select(static member => ToUInt64Literal(member.UInt64Value)));
        sb.AppendLine(" };");

        sb.Append("    private static readonly string[] s_flagMetadataNames_").Append(enumIndex).Append(" = new string[] { ");
        AppendCommaSeparated(sb, flagMembers.Select(static member => ToLiteral(member.MetadataName ?? member.Name)));
        sb.AppendLine(" };");
        sb.AppendLine();
    }

    private static void AppendTryParseHelpers(StringBuilder sb, string enumTypeName, EnumToProcess enumeration, int enumIndex)
    {
        sb.Append("    private static bool TryParseSingle_").Append(enumIndex).Append("(global::System.ReadOnlySpan<char> value, bool ignoreCase, bool useMetadata, out ").Append(enumTypeName).AppendLine(" result)");
        sb.AppendLine("    {");
        sb.Append("        for (var i = 0; i < s_parseTokens_").Append(enumIndex).AppendLine(".Length; i++)");
        sb.AppendLine("        {");
        sb.Append("            if (!useMetadata && s_parseTokenIsMetadata_").Append(enumIndex).AppendLine("[i])");
        sb.AppendLine("                continue;");
        sb.AppendLine();
        sb.Append("            if (EqualsToken_").Append(enumIndex).Append("(value, s_parseTokens_").Append(enumIndex).AppendLine("[i], ignoreCase))");
        sb.AppendLine("            {");
        sb.Append("                result = s_parseValues_").Append(enumIndex).AppendLine("[i];");
        sb.AppendLine("                return true;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        result = default;");
        sb.AppendLine("        return false;");
        sb.AppendLine("    }");
        sb.AppendLine();

        if (enumeration.IsFlags)
        {
            sb.Append("    private static bool TryParseFlags_").Append(enumIndex).Append("(global::System.ReadOnlySpan<char> value, bool ignoreCase, bool useMetadata, out ").Append(enumTypeName).AppendLine(" result)");
            sb.AppendLine("    {");
            sb.AppendLine("        var remaining = value;");
            sb.AppendLine("        var hasToken = false;");
            sb.AppendLine("        var parsedValue = 0UL;");
            sb.AppendLine();
            sb.AppendLine("        while (true)");
            sb.AppendLine("        {");
            sb.AppendLine("            var separatorIndex = -1;");
            sb.AppendLine("            for (var i = 0; i < remaining.Length; i++)");
            sb.AppendLine("            {");
            sb.AppendLine("                if (remaining[i] != ',')");
            sb.AppendLine("                    continue;");
            sb.AppendLine();
            sb.AppendLine("                separatorIndex = i;");
            sb.AppendLine("                break;");
            sb.AppendLine("            }");
            sb.AppendLine("            var token = separatorIndex >= 0 ? remaining[..separatorIndex] : remaining;");
            sb.Append("            token = TrimToken_").Append(enumIndex).AppendLine("(token);");
            sb.AppendLine("            if (token.IsEmpty)");
            sb.AppendLine("            {");
            sb.AppendLine("                result = default;");
            sb.AppendLine("                return false;");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.Append("            if (!TryParseSingle_").Append(enumIndex).AppendLine("(token, ignoreCase, useMetadata, out var tokenValue))");
            sb.AppendLine("            {");
            sb.AppendLine("                result = default;");
            sb.AppendLine("                return false;");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.Append("            parsedValue |= ToUInt64_").Append(enumIndex).AppendLine("(tokenValue);");
            sb.AppendLine("            hasToken = true;");
            sb.AppendLine("            if (separatorIndex < 0)");
            sb.AppendLine("                break;");
            sb.AppendLine();
            sb.AppendLine("            remaining = remaining[(separatorIndex + 1)..];");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        if (!hasToken)");
            sb.AppendLine("        {");
            sb.AppendLine("            result = default;");
            sb.AppendLine("            return false;");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.Append("        result = FromUInt64_").Append(enumIndex).AppendLine("(parsedValue);");
            sb.AppendLine("        return true;");
            sb.AppendLine("    }");
            sb.AppendLine();

            sb.Append("    private static global::System.ReadOnlySpan<char> TrimToken_").Append(enumIndex).AppendLine("(global::System.ReadOnlySpan<char> value)");
            sb.AppendLine("    {");
            sb.AppendLine("        var start = 0;");
            sb.AppendLine("        while (start < value.Length && char.IsWhiteSpace(value[start]))");
            sb.AppendLine("        {");
            sb.AppendLine("            start++;");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        var end = value.Length - 1;");
            sb.AppendLine("        while (end >= start && char.IsWhiteSpace(value[end]))");
            sb.AppendLine("        {");
            sb.AppendLine("            end--;");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        if (end < start)");
            sb.AppendLine("            return global::System.ReadOnlySpan<char>.Empty;");
            sb.AppendLine();
            sb.AppendLine("        return value[start..(end + 1)];");
            sb.AppendLine("    }");
            sb.AppendLine();
        }
    }

    private static void AppendFormatFlagsName(StringBuilder sb, string enumTypeName, int enumIndex)
    {
        sb.Append("    private static string FormatFlagsName_").Append(enumIndex).Append('(').Append(enumTypeName).AppendLine(" value)");
        sb.AppendLine("    {");
        sb.Append("        var remaining = ToUInt64_").Append(enumIndex).AppendLine("(value);");
        sb.AppendLine("        if (remaining == 0)");
        sb.AppendLine("            return value.ToString();");
        sb.AppendLine();
        sb.AppendLine("        var sb = new global::System.Text.StringBuilder();");
        sb.AppendLine("        var hasValue = false;");
        sb.Append("        for (var i = 0; i < s_flagValues_").Append(enumIndex).AppendLine(".Length; i++)");
        sb.AppendLine("        {");
        sb.Append("            var flagValue = s_flagValues_").Append(enumIndex).AppendLine("[i];");
        sb.AppendLine("            if ((remaining & flagValue) != flagValue)");
        sb.AppendLine("                continue;");
        sb.AppendLine();
        sb.AppendLine("            if (hasValue)");
        sb.AppendLine("                sb.Append(\", \");");
        sb.AppendLine();
        sb.Append("            sb.Append(s_flagMetadataNames_").Append(enumIndex).AppendLine("[i]);");
        sb.AppendLine("            hasValue = true;");
        sb.AppendLine("            remaining &= ~flagValue;");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        return remaining == 0 && hasValue ? sb.ToString() : value.ToString();");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

