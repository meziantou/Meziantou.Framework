    private static List<EnumMemberToProcess> GetUniqueMembers(ImmutableArray<EnumMemberToProcess> members)
    {
        var result = new List<EnumMemberToProcess>(members.Length);
        var values = new HashSet<ulong>();
        foreach (var member in members)
        {
            if (values.Add(member.UInt64Value))
            {
                result.Add(member);
            }
        }

        return result;
    }

    private static List<ParseTokenToProcess> GetParseTokens(ImmutableArray<EnumMemberToProcess> members)
    {
        var result = new List<ParseTokenToProcess>(members.Length * 2);
        var values = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        foreach (var member in members)
        {
            Add(member.Name, member.Name, isMetadata: false);
            if (!string.IsNullOrEmpty(member.MetadataName))
            {
                Add(member.MetadataName, member.Name, isMetadata: true);
            }
        }

        return result;

        void Add(string token, string memberName, bool isMetadata)
        {
            if (!values.Add(token))
                return;

            result.Add(new ParseTokenToProcess(token, memberName, isMetadata));
        }
    }

    private static bool IsPowerOfTwo(ulong value)
    {
        return value != 0 && (value & (value - 1)) == 0;
    }

    private static string ToLiteral(string value)
    {
        return SymbolDisplay.FormatLiteral(value, quote: true);
    }

    private static string ToUInt64Literal(ulong value)
    {
        return "0x" + value.ToString("X", CultureInfo.InvariantCulture) + "UL";
    }

    private static void AppendCommaSeparated(StringBuilder sb, IEnumerable<string> values)
    {
        var isFirst = true;
        foreach (var value in values)
        {
            if (!isFirst)
            {
                sb.Append(", ");
            }

            sb.Append(value);
            isFirst = false;
        }
    }

    private static bool IsVisibleOutsideOfAssembly(ISymbol? symbol)
    {
        if (symbol is null)
            return false;

        if (symbol.DeclaredAccessibility is not Accessibility.Public and not Accessibility.Protected and not Accessibility.ProtectedOrInternal)
            return false;

        if (symbol.ContainingType is null)
            return true;

        return IsVisibleOutsideOfAssembly(symbol.ContainingType);
    }

    private sealed record EnumToProcess(INamedTypeSymbol EnumSymbol, ImmutableArray<EnumMemberToProcess> Members, bool IsPublic, string? Namespace, bool IsFlags, bool IsZeroBasedConsecutive, string UnderlyingTypeName, bool IsUnderlyingTypeSigned)
    {
        public string FullCsharpName { get; } = EnumSymbol.ToString()!;
        public string? FullNamespace { get; } = Namespace ?? GetNamespace(EnumSymbol);

        private static string? GetNamespace(ITypeSymbol symbol)
        {
            string? result = null;
            var ns = symbol.ContainingNamespace;
            while (ns is not null && !ns.IsGlobalNamespace)
            {
                result = result is null ? ns.Name : ns.Name + "." + result;
                ns = ns.ContainingNamespace;
            }

            return result;
        }
    }

