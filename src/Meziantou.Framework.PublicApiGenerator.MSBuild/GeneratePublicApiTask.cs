using Microsoft.Build.Framework;
using Microsoft.Build.Utilities;
using Meziantou.Framework.PublicApiGenerator;
using System.Diagnostics.CodeAnalysis;

namespace Meziantou.Framework.PublicApiGenerator.MSBuild;

public sealed class GeneratePublicApiTask : Microsoft.Build.Utilities.Task
{
    [SuppressMessage("Performance", "CA1819:Properties should not return arrays", Justification = "MSBuild task item parameters are represented as arrays.")]
    [Required]
    public ITaskItem[] Assemblies { get; set; } = [];

    [Required]
    public string OutputPath { get; set; } = string.Empty;

    public string FileLayout { get; set; } = nameof(PublicApiFileLayout.SingleFile);

    public bool IncludeAutoGeneratedComment { get; set; } = true;

    public bool VerifyNoChange { get; set; }

    public override bool Execute()
    {
        if (Assemblies.Length == 0)
        {
            Log.LogError("At least one assembly must be provided.");
            return false;
        }

        if (!Enum.TryParse(FileLayout, ignoreCase: true, out PublicApiFileLayout fileLayout))
        {
            Log.LogError("Invalid file layout '{0}'. Supported values are: {1}.", FileLayout, string.Join(", ", Enum.GetNames<PublicApiFileLayout>()));
            return false;
        }

        var assemblySources = new List<AssemblySource>(Assemblies.Length);
        foreach (var assembly in Assemblies
            .OrderBy(item => item.GetMetadata("TargetFramework"), StringComparer.Ordinal)
            .ThenBy(item => item.ItemSpec, StringComparer.Ordinal))
        {
            var assemblyPath = FullPath.FromPath(assembly.ItemSpec);
            if (!File.Exists(assemblyPath))
            {
                Log.LogError("The assembly '{0}' does not exist.", assemblyPath);
                return false;
            }

            var targetFramework = assembly.GetMetadata("TargetFramework");
            assemblySources.Add(string.IsNullOrWhiteSpace(targetFramework) ? new AssemblySource(assemblyPath) : new AssemblySource(assemblyPath, targetFramework));
        }

        var options = new PublicApiOptions
        {
            FileLayout = fileLayout,
            IncludeAutoGeneratedComment = IncludeAutoGeneratedComment,
        };

        if (options.FileLayout is PublicApiFileLayout.SingleFile)
        {
            var files = PublicApi.Generate(assemblySources, options);
            if (files.Count != 1)
            {
                Log.LogError("Expected a single generated file when using {0} layout.", nameof(PublicApiFileLayout.SingleFile));
                return false;
            }

            var outputFilePath = FullPath.FromPath(OutputPath);
            if (VerifyNoChange)
            {
                LogValidationErrors(ValidateSingleFile(files[0], outputFilePath));
                return !Log.HasLoggedErrors;
            }

            outputFilePath.CreateParentDirectory();
            File.WriteAllText(outputFilePath, files[0].Content);
            return !Log.HasLoggedErrors;
        }

        var outputDirectoryPath = FullPath.FromPath(OutputPath);
        if (VerifyNoChange)
        {
            var files = PublicApi.Generate(assemblySources, options);
            LogValidationErrors(ValidateDirectory(files, outputDirectoryPath));
            return !Log.HasLoggedErrors;
        }

        Directory.CreateDirectory(outputDirectoryPath);
        PublicApi.GenerateToDirectory(assemblySources, outputDirectoryPath, options);
        return !Log.HasLoggedErrors;
    }

    private void LogValidationErrors(IReadOnlyList<string> errors)
    {
        foreach (var error in errors)
        {
            Log.LogError(error);
        }
    }

    private static List<string> ValidateSingleFile(PublicApiFile expectedFile, FullPath outputFilePath)
    {
        var errors = new List<string>();
        if (!File.Exists(outputFilePath))
        {
            errors.Add($"Expected file '{outputFilePath}' does not exist.");
            return errors;
        }

        var actualContent = File.ReadAllText(outputFilePath);
        if (!AreContentsEqual(expectedFile.Content, actualContent))
        {
            errors.Add($"File '{outputFilePath}' is out of date.");
        }

        return errors;
    }

    private static List<string> ValidateDirectory(IReadOnlyList<PublicApiFile> expectedFiles, FullPath outputDirectoryPath)
    {
        var errors = new List<string>();
        var comparer = GetRelativePathComparer();
        var expectedFilesByRelativePath = expectedFiles
            .GroupBy(file => NormalizeRelativePath(file.RelativePath), comparer)
            .ToDictionary(group => group.Key, group => group.Last(), comparer);

        foreach (var expectedFile in expectedFilesByRelativePath.Values)
        {
            var outputFilePath = outputDirectoryPath / expectedFile.RelativePath;
            if (!File.Exists(outputFilePath))
            {
                errors.Add($"Expected file '{outputFilePath}' does not exist.");
                continue;
            }

            var actualContent = File.ReadAllText(outputFilePath);
            if (!AreContentsEqual(expectedFile.Content, actualContent))
            {
                errors.Add($"File '{outputFilePath}' is out of date.");
            }
        }

        if (Directory.Exists(outputDirectoryPath))
        {
            foreach (var outputFilePath in Directory.EnumerateFiles(outputDirectoryPath, "*", SearchOption.AllDirectories).Select(FullPath.FromPath))
            {
                var relativePath = NormalizeRelativePath(outputFilePath.MakePathRelativeTo(outputDirectoryPath));
                if (!expectedFilesByRelativePath.ContainsKey(relativePath))
                {
                    errors.Add($"Unexpected file '{outputFilePath}' exists in the output directory.");
                }
            }
        }

        return errors;
    }

    private static bool AreContentsEqual(string expected, string actual)
    {
        return string.Equals(NormalizeLineEndings(expected), NormalizeLineEndings(actual), StringComparison.Ordinal);
    }

    private static string NormalizeLineEndings(string value)
    {
        return value.ReplaceLineEndings("\n");
    }

    private static string NormalizeRelativePath(string path)
    {
        return path.Replace('\\', '/');
    }

    private static StringComparer GetRelativePathComparer()
    {
        return OperatingSystem.IsLinux() ? StringComparer.Ordinal : StringComparer.OrdinalIgnoreCase;
    }
}
