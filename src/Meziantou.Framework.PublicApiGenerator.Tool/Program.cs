using System.CommandLine;
using System.CommandLine.Invocation;
using Meziantou.Framework;
using Meziantou.Framework.PublicApiGenerator;

[assembly: System.Runtime.CompilerServices.InternalsVisibleTo("Meziantou.Framework.PublicApiGenerator.Tests")]

namespace Meziantou.Framework.PublicApiGenerator.Tool;

internal static class Program
{
    public static Task<int> Main(string[] args)
    {
        return MainImpl(args, configure: null);
    }

    internal static Task<int> MainImpl(string[] args, Action<InvocationConfiguration>? configure)
    {
        var inputOption = new Option<string[]>("--input")
        {
            Description = "Assembly input. Use either 'path' or 'tfm=path'. Repeat for multi-target generation.",
            Required = true,
            Arity = ArgumentArity.OneOrMore,
            AllowMultipleArgumentsPerToken = true,
        };
        var outputOption = new Option<string>("--output")
        {
            Description = "Output directory",
        };
        var outputFileOption = new Option<string>("--output-file")
        {
            Description = "Output file path when --file-layout is SingleFile",
        };
        var fileLayoutOption = new Option<PublicApiFileLayout>("--file-layout")
        {
            Description = "File layout: SingleFile, OneFilePerNamespace, or OneFilePerType",
            DefaultValueFactory = _ => PublicApiFileLayout.SingleFile,
        };
        var omitAutoGeneratedCommentOption = new Option<bool>("--omit-auto-generated-comment")
        {
            Description = "Omit the // <auto-generated/> comment from generated files",
        };
        var verifyNoChangeOption = new Option<bool>("--verify-no-change")
        {
            Description = "Verify generated files are up to date without writing changes",
        };

        var rootCommand = new RootCommand("Generate public API stubs from a .NET assembly.");
        rootCommand.Options.Add(inputOption);
        rootCommand.Options.Add(outputOption);
        rootCommand.Options.Add(outputFileOption);
        rootCommand.Options.Add(fileLayoutOption);
        rootCommand.Options.Add(omitAutoGeneratedCommentOption);
        rootCommand.Options.Add(verifyNoChangeOption);

        rootCommand.SetAction(parseResult =>
        {
            var inputValues = parseResult.GetValue(inputOption);
            if (inputValues is null || inputValues.Length == 0)
            {
                throw new InvalidOperationException("The --input option is required.");
            }

            var options = new PublicApiOptions
            {
                FileLayout = parseResult.GetValue(fileLayoutOption),
                IncludeAutoGeneratedComment = !parseResult.GetValue(omitAutoGeneratedCommentOption),
            };

            var inputs = ParseInputs(inputValues);
            var output = parseResult.GetValue(outputOption);
            var outputFile = parseResult.GetValue(outputFileOption);
            var verifyNoChange = parseResult.GetValue(verifyNoChangeOption);
            GenerateFiles(inputs, output, outputFile, options, verifyNoChange);

            return 0;
        });

        var invocationConfiguration = new InvocationConfiguration();
        configure?.Invoke(invocationConfiguration);
        return rootCommand.Parse(args).InvokeAsync(invocationConfiguration);
    }

    private static void GenerateFiles(IReadOnlyList<AssemblySource> inputs, string? outputDirectory, string? outputFile, PublicApiOptions options, bool verifyNoChange)
    {
        if (options.FileLayout is PublicApiFileLayout.SingleFile)
        {
            if (!string.IsNullOrWhiteSpace(outputDirectory) && !string.IsNullOrWhiteSpace(outputFile))
            {
                throw new InvalidOperationException("Only one of the --output and --output-file options can be specified when --file-layout is SingleFile.");
            }

            if (!string.IsNullOrWhiteSpace(outputFile))
            {
                var files = PublicApi.Generate(inputs, options);
                if (files.Count != 1)
                {
                    throw new InvalidOperationException($"Expected a single generated file when using {nameof(PublicApiFileLayout.SingleFile)} layout.");
                }

                var outputFilePath = FullPath.FromPath(outputFile);
                if (verifyNoChange)
                {
                    ThrowIfValidationFailed(ValidateSingleFile(files[0], outputFilePath));
                    return;
                }

                outputFilePath.CreateParentDirectory();
                File.WriteAllText(outputFilePath, files[0].Content);
                return;
            }

            if (!string.IsNullOrWhiteSpace(outputDirectory))
            {
                if (verifyNoChange)
                {
                    var files = PublicApi.Generate(inputs, options);
                    var outputDirectoryPath = FullPath.FromPath(outputDirectory);
                    ThrowIfValidationFailed(ValidateDirectory(files, outputDirectoryPath));
                    return;
                }

                PublicApi.GenerateToDirectory(inputs, outputDirectory, options);
                return;
            }

            throw new InvalidOperationException("Either --output or --output-file is required when --file-layout is SingleFile.");
        }

        if (!string.IsNullOrWhiteSpace(outputFile))
        {
            throw new InvalidOperationException("The --output-file option can only be used when --file-layout is SingleFile.");
        }

        if (string.IsNullOrWhiteSpace(outputDirectory))
        {
            throw new InvalidOperationException("The --output option is required.");
        }

        if (verifyNoChange)
        {
            var files = PublicApi.Generate(inputs, options);
            var outputDirectoryPath = FullPath.FromPath(outputDirectory);
            ThrowIfValidationFailed(ValidateDirectory(files, outputDirectoryPath));
            return;
        }

        PublicApi.GenerateToDirectory(inputs, outputDirectory, options);
    }

    private static void ThrowIfValidationFailed(List<string> errors)
    {
        if (errors.Count == 0)
        {
            return;
        }

        throw new InvalidOperationException("Public API validation failed:\n" + string.Join('\n', errors.Select(error => " - " + error)));
    }

    private static List<string> ValidateSingleFile(PublicApiFile expectedFile, FullPath outputFilePath)
    {
        var errors = new List<string>();
        if (!File.Exists(outputFilePath))
        {
            errors.Add($"Expected file '{outputFilePath}' does not exist.");
            return errors;
        }

        var actualContent = File.ReadAllText(outputFilePath);
        if (!AreContentsEqual(expectedFile.Content, actualContent))
        {
            errors.Add($"File '{outputFilePath}' is out of date.");
        }

        return errors;
    }

    private static List<string> ValidateDirectory(IReadOnlyList<PublicApiFile> expectedFiles, FullPath outputDirectoryPath)
    {
        var errors = new List<string>();
        var comparer = GetRelativePathComparer();
        var expectedFilesByRelativePath = expectedFiles
            .GroupBy(file => file.RelativePath, comparer)
            .ToDictionary(group => group.Key, group => group.Last(), comparer);

        foreach (var expectedFile in expectedFilesByRelativePath.Values)
        {
            var outputFilePath = outputDirectoryPath / expectedFile.RelativePath;
            if (!File.Exists(outputFilePath))
            {
                errors.Add($"Expected file '{outputFilePath}' does not exist.");
                continue;
            }

            var actualContent = File.ReadAllText(outputFilePath);
            if (!AreContentsEqual(expectedFile.Content, actualContent))
            {
                errors.Add($"File '{outputFilePath}' is out of date.");
            }
        }

        if (Directory.Exists(outputDirectoryPath))
        {
            foreach (var outputFilePath in Directory.EnumerateFiles(outputDirectoryPath, "*", SearchOption.AllDirectories).Select(FullPath.FromPath))
            {
                var relativePath = outputFilePath.MakePathRelativeTo(outputDirectoryPath);
                if (!expectedFilesByRelativePath.ContainsKey(relativePath))
                {
                    errors.Add($"Unexpected file '{outputFilePath}' exists in the output directory.");
                }
            }
        }

        return errors;
    }

    private static bool AreContentsEqual(string expected, string actual)
    {
        return string.Equals(NormalizeLineEndings(expected), NormalizeLineEndings(actual), StringComparison.Ordinal);
    }

    private static string NormalizeLineEndings(string value)
    {
        return value.ReplaceLineEndings("\n");
    }

    private static StringComparer GetRelativePathComparer()
    {
        return OperatingSystem.IsLinux() ? StringComparer.Ordinal : StringComparer.OrdinalIgnoreCase;
    }

    private static List<AssemblySource> ParseInputs(string[] inputValues)
    {
        var result = new List<AssemblySource>(inputValues.Length);
        foreach (var inputValue in inputValues)
        {
            if (string.IsNullOrWhiteSpace(inputValue))
            {
                throw new InvalidOperationException("The --input option cannot be empty.");
            }

            var trimmedInputValue = inputValue.Trim();
            var separatorIndex = trimmedInputValue.IndexOf('=', StringComparison.Ordinal);
            if (separatorIndex < 0)
            {
                result.Add(trimmedInputValue);
                continue;
            }

            if (separatorIndex == 0 || separatorIndex == trimmedInputValue.Length - 1)
            {
                throw new InvalidOperationException($"Invalid --input value '{inputValue}'. Use either 'path' or 'tfm=path'.");
            }

            var targetFramework = trimmedInputValue[..separatorIndex].Trim();
            var assemblyPath = trimmedInputValue[(separatorIndex + 1)..].Trim();
            if (string.IsNullOrEmpty(targetFramework) || string.IsNullOrEmpty(assemblyPath))
            {
                throw new InvalidOperationException($"Invalid --input value '{inputValue}'. Use either 'path' or 'tfm=path'.");
            }

            result.Add(new AssemblySource(assemblyPath, targetFramework));
        }

        return result;
    }
}
