using System.Text;

namespace Meziantou.Framework.PublicApiGenerator;

internal static class PublicApiEmitter
{
    public static IReadOnlyList<PublicApiFile> Generate(PublicApiModel model, PublicApiOptions options)
    {
        ArgumentNullException.ThrowIfNull(options);

        return options.FileLayout switch
        {
            PublicApiFileLayout.SingleFile => [new PublicApiFile("PublicApi.g.cs", BuildSingleFile(model, options.IncludeAutoGeneratedComment))],
            PublicApiFileLayout.OneFilePerNamespace => BuildNamespaceFiles(model, options.IncludeAutoGeneratedComment),
            PublicApiFileLayout.OneFilePerType => BuildTypeFiles(model, options.IncludeAutoGeneratedComment),
            _ => throw new ArgumentOutOfRangeException(nameof(options)),
        };
    }

    private static List<PublicApiFile> BuildNamespaceFiles(PublicApiModel model, bool includeAutoGeneratedComment)
    {
        var files = new List<PublicApiFile>();
        foreach (var group in model.Types.GroupBy(static type => type.Namespace, StringComparer.Ordinal).OrderBy(static group => group.Key, StringComparer.Ordinal))
        {
            var fileName = string.IsNullOrEmpty(group.Key) ? "GlobalNamespace.g.cs" : SanitizeFileName(group.Key) + ".g.cs";
            files.Add(new PublicApiFile(fileName, BuildFileContent(group.Key, group.OrderBy(static type => type.QualifiedName, StringComparer.Ordinal), includeAutoGeneratedComment)));
        }

        return files;
    }

    private static List<PublicApiFile> BuildTypeFiles(PublicApiModel model, bool includeAutoGeneratedComment)
    {
        var files = new List<PublicApiFile>(model.Types.Length);
        foreach (var type in model.Types.OrderBy(static value => value.QualifiedName, StringComparer.Ordinal))
        {
            var fileName = SanitizeFileName(type.QualifiedName) + ".g.cs";
            files.Add(new PublicApiFile(fileName, BuildFileContent(type.Namespace, [type], includeAutoGeneratedComment)));
        }

        return files;
    }

    private static string BuildSingleFile(PublicApiModel model, bool includeAutoGeneratedComment)
    {
        var sb = CreateFileHeader(includeAutoGeneratedComment);
        var groupedTypes = model.Types.GroupBy(static type => type.Namespace, StringComparer.Ordinal).OrderBy(static group => group.Key, StringComparer.Ordinal);
        foreach (var group in groupedTypes)
        {
            AppendNamespace(sb, group.Key, group.OrderBy(static type => type.QualifiedName, StringComparer.Ordinal));
        }

        return sb.ToString();
    }

    private static string BuildFileContent(string namespaceName, IEnumerable<PublicApiTypeModel> types, bool includeAutoGeneratedComment)
    {
        var sb = CreateFileHeader(includeAutoGeneratedComment);
        AppendNamespace(sb, namespaceName, types);
        return sb.ToString();
    }

    private static StringBuilder CreateFileHeader(bool includeAutoGeneratedComment)
    {
        var sb = new StringBuilder();
        if (includeAutoGeneratedComment)
        {
            sb.AppendLine("// <auto-generated/>");
        }

        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        return sb;
    }

    private static void AppendNamespace(StringBuilder sb, string namespaceName, IEnumerable<PublicApiTypeModel> types)
    {
        var orderedTypes = types.OrderBy(static type => type.QualifiedName, StringComparer.Ordinal).ToList();
        if (orderedTypes.Count == 0)
            return;

        var conflictingNamespaceIdentifiers = GetConflictingNamespaceIdentifiers(namespaceName);
        if (string.IsNullOrEmpty(namespaceName))
        {
            for (var i = 0; i < orderedTypes.Count; i++)
            {
                if (i > 0)
                {
                    sb.AppendLine();
                }

                var source = conflictingNamespaceIdentifiers.Count > 0 ? QualifyConflictingNamespaceReferences(orderedTypes[i].Source, conflictingNamespaceIdentifiers) : orderedTypes[i].Source;
                sb.AppendLine(source);
            }

            return;
        }

        sb.Append("namespace ");
        sb.AppendLine(namespaceName);
        sb.AppendLine("{");
        for (var i = 0; i < orderedTypes.Count; i++)
        {
            if (i > 0)
            {
                sb.AppendLine();
            }

            var source = conflictingNamespaceIdentifiers.Count > 0 ? QualifyConflictingNamespaceReferences(orderedTypes[i].Source, conflictingNamespaceIdentifiers) : orderedTypes[i].Source;
            AppendIndented(sb, source, 1);
        }

        sb.AppendLine("}");
    }

    private static HashSet<string> GetConflictingNamespaceIdentifiers(string namespaceName)
    {
        if (string.IsNullOrEmpty(namespaceName))
            return [];

        var identifiers = new HashSet<string>(StringComparer.Ordinal);
        foreach (var segment in namespaceName.Split('.', StringSplitOptions.RemoveEmptyEntries))
        {
            identifiers.Add(segment);
        }

        return identifiers;
    }

    private static string QualifyConflictingNamespaceReferences(string source, HashSet<string> conflictingNamespaceIdentifiers)
    {
        var sb = new StringBuilder(source.Length);
        var inString = false;
        var inChar = false;
        var escapeNext = false;
        for (var i = 0; i < source.Length; i++)
        {
            if (!inString && !inChar && TryGetConflictingIdentifierLength(source, i, conflictingNamespaceIdentifiers, out var identifierLength))
            {
                sb.Append("global::");
                sb.Append(source, i, identifierLength + 1);
                i += identifierLength;
                continue;
            }

            var ch = source[i];
            sb.Append(ch);

            if (inString)
            {
                if (escapeNext)
                {
                    escapeNext = false;
                }
                else if (ch == '\\')
                {
                    escapeNext = true;
                }
                else if (ch == '"')
                {
                    inString = false;
                }

                continue;
            }

            if (inChar)
            {
                if (escapeNext)
                {
                    escapeNext = false;
                }
                else if (ch == '\\')
                {
                    escapeNext = true;
                }
                else if (ch == '\'')
                {
                    inChar = false;
                }

                continue;
            }

            if (ch == '"')
            {
                inString = true;
                continue;
            }

            if (ch == '\'')
            {
                inChar = true;
            }
        }

        return sb.ToString();
    }

    private static bool TryGetConflictingIdentifierLength(string source, int index, HashSet<string> conflictingNamespaceIdentifiers, out int identifierLength)
    {
        identifierLength = 0;
        if (index >= source.Length || (!char.IsLetter(source[index]) && source[index] != '_'))
            return false;

        var currentIndex = index + 1;
        while (currentIndex < source.Length && (char.IsLetterOrDigit(source[currentIndex]) || source[currentIndex] == '_'))
        {
            currentIndex++;
        }

        if (currentIndex >= source.Length || source[currentIndex] != '.')
            return false;

        var identifier = source[index..currentIndex];
        if (!conflictingNamespaceIdentifiers.Contains(identifier))
            return false;

        if (index >= "global::".Length &&
            source[index - 8] == 'g' &&
            source[index - 7] == 'l' &&
            source[index - 6] == 'o' &&
            source[index - 5] == 'b' &&
            source[index - 4] == 'a' &&
            source[index - 3] == 'l' &&
            source[index - 2] == ':' &&
            source[index - 1] == ':')
        {
            return false;
        }

        if (index == 0)
        {
            identifierLength = identifier.Length;
            return true;
        }

        var previousCharacter = source[index - 1];
        if (char.IsLetterOrDigit(previousCharacter) || previousCharacter is '_' or '.')
            return false;

        identifierLength = identifier.Length;
        return true;
    }

    private static void AppendIndented(StringBuilder sb, string text, int indentationLevel)
    {
        using var reader = new StringReader(text);
        string? line;
        while ((line = reader.ReadLine()) is not null)
        {
            if (line.Length > 0)
            {
                sb.Append(' ', indentationLevel * 4);
            }

            sb.AppendLine(line);
        }
    }

    private static string SanitizeFileName(string value)
    {
        var invalidChars = Path.GetInvalidFileNameChars();
        var sanitized = new StringBuilder(value.Length);
        foreach (var character in value)
        {
            sanitized.Append(invalidChars.Contains(character) ? '_' : character);
        }

        return sanitized.ToString();
    }
}
