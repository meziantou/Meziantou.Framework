using System.CommandLine;
using System.Runtime.CompilerServices;
using Meziantou.Framework;

[assembly: InternalsVisibleTo("Meziantou.Framework.SnapshotTesting.Tool.Tests")]

namespace Meziantou.Framework.SnapshotTesting.Tool;

internal static class Program
{
    private const string ActualMarker = ".actual.";

    public static Task<int> Main(string[] args)
    {
        return MainImpl(args, configure: null);
    }

    internal static Task<int> MainImpl(string[] args, Action<InvocationConfiguration>? configure)
    {
        return MainImpl(args, configure, input: null);
    }

    internal static Task<int> MainImpl(string[] args, Action<InvocationConfiguration>? configure, TextReader? input)
    {
        var rootCommand = new RootCommand("Approve snapshots generated by Meziantou.Framework.SnapshotTesting.");

        var interactiveOption = new Option<bool>("--interactive")
        {
            Description = "Prompt for each snapshot to approve or reject",
            DefaultValueFactory = _ => false,
        };
        var folderOption = new Option<string?>("--folder")
        {
            Description = "Folder to process. Uses current directory when not set.",
        };
        var recurseOption = new Option<bool>("--recurse")
        {
            Description = "Allow recursive search for __snapshots__ folders",
            DefaultValueFactory = _ => true,
        };

        var approveCommand = new Command("approve")
        {
            Description = "Approve snapshots by renaming *.actual.* files to *.verified.*",
        };
        approveCommand.Options.Add(interactiveOption);
        approveCommand.Options.Add(folderOption);
        approveCommand.Options.Add(recurseOption);
        approveCommand.SetAction(parseResult =>
        {
            return ApproveSnapshots(
                parseResult.GetValue(interactiveOption),
                parseResult.GetValue(folderOption),
                parseResult.GetValue(recurseOption),
                input,
                parseResult.InvocationConfiguration.Output,
                parseResult.InvocationConfiguration.Error);
        });

        rootCommand.Subcommands.Add(approveCommand);

        var invocationConfiguration = new InvocationConfiguration();
        configure?.Invoke(invocationConfiguration);
        return rootCommand.Parse(args).InvokeAsync(invocationConfiguration);
    }

    private static int ApproveSnapshots(bool interactive, string? folder, bool recurse, TextReader? input, TextWriter output, TextWriter error)
    {
        var rootPath = string.IsNullOrWhiteSpace(folder) ? FullPath.CurrentDirectory() : FullPath.FromPath(folder);
        if (!Directory.Exists(rootPath))
        {
            error.WriteLine($"Folder '{rootPath}' does not exist.");
            return 1;
        }

        var actualFiles = EnumerateActualSnapshotFiles(rootPath, recurse)
            .OrderBy(path => path.Value, StringComparer.Ordinal)
            .ToArray();

        var approvedCount = 0;
        var skippedCount = 0;
        var failureCount = 0;
        var reader = input ?? Console.In;

        foreach (var actualFile in actualFiles)
        {
            var verifiedFile = GetVerifiedPath(actualFile);
            if (interactive && !ShouldApproveSnapshot(reader, output, actualFile, verifiedFile))
            {
                skippedCount++;
                continue;
            }

            try
            {
                verifiedFile.CreateParentDirectory();
                File.Move(actualFile, verifiedFile, overwrite: true);
                approvedCount++;
            }
            catch (Exception ex)
            {
                failureCount++;
                error.WriteLine($"Failed to approve '{actualFile}': {ex.Message}");
            }
        }

        output.WriteLine($"Found {actualFiles.Length} snapshot(s). Approved: {approvedCount}. Skipped: {skippedCount}. Failures: {failureCount}.");
        return failureCount is 0 ? 0 : 1;
    }

    private static IEnumerable<FullPath> EnumerateActualSnapshotFiles(FullPath rootPath, bool recurse)
    {
        foreach (var snapshotDirectory in EnumerateSnapshotDirectories(rootPath, recurse))
        {
            foreach (var file in Directory.EnumerateFiles(snapshotDirectory, "*.actual.*", SearchOption.TopDirectoryOnly))
            {
                yield return FullPath.FromPath(file);
            }
        }
    }

    private static IEnumerable<FullPath> EnumerateSnapshotDirectories(FullPath rootPath, bool recurse)
    {
        if (string.Equals(rootPath.Name, "__snapshots__", StringComparison.Ordinal))
        {
            yield return rootPath;
        }
        else
        {
            var localSnapshotDirectory = rootPath / "__snapshots__";
            if (Directory.Exists(localSnapshotDirectory))
            {
                yield return localSnapshotDirectory;
            }
        }

        if (!recurse)
            yield break;

        var rootSnapshotDirectory = rootPath / "__snapshots__";
        foreach (var directory in Directory.EnumerateDirectories(rootPath, "__snapshots__", SearchOption.AllDirectories))
        {
            var snapshotDirectory = FullPath.FromPath(directory);
            if (string.Equals(snapshotDirectory.Value, rootSnapshotDirectory.Value, StringComparison.Ordinal))
            {
                continue;
            }

            yield return snapshotDirectory;
        }
    }

    private static FullPath GetVerifiedPath(FullPath actualFile)
    {
        var fileName = actualFile.Name;
        var markerIndex = fileName.LastIndexOf(ActualMarker, StringComparison.Ordinal);
        if (markerIndex < 0)
            throw new InvalidOperationException($"Invalid snapshot file name '{fileName}'. Expected '.actual.' marker.");

        var verifiedName = fileName[..markerIndex] + ".verified." + fileName[(markerIndex + ActualMarker.Length)..];
        return actualFile.Parent / verifiedName;
    }

    private static bool ShouldApproveSnapshot(TextReader input, TextWriter output, FullPath actualFile, FullPath verifiedFile)
    {
        while (true)
        {
            output.Write($"Approve '{actualFile.Name}' => '{verifiedFile.Name}'? [y/N] ");
            var answer = input.ReadLine();
            if (answer is null)
                return false;

            if (string.Equals(answer, "y", StringComparison.OrdinalIgnoreCase) || string.Equals(answer, "yes", StringComparison.OrdinalIgnoreCase))
                return true;

            if (string.IsNullOrEmpty(answer) || string.Equals(answer, "n", StringComparison.OrdinalIgnoreCase) || string.Equals(answer, "no", StringComparison.OrdinalIgnoreCase))
                return false;

            output.WriteLine("Please answer 'y' or 'n'.");
        }
    }
}
