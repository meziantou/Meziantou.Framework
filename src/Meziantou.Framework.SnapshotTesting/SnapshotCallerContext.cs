using System.Diagnostics;
using System.Diagnostics.CodeAnalysis;
using System.Reflection;
using System.Runtime.CompilerServices;
using System.Text.RegularExpressions;

namespace Meziantou.Framework.SnapshotTesting;

internal sealed record SnapshotCallerContext(FullPath SourceFilePath, string MethodName, string? MemberName, int LineNumber)
{
    /// <summary>
    /// Newer Roslyn versions use the format "&lt;callerName&gt;g__functionName|x_y".
    /// Older versions use "&lt;callerName&gt;g__functionNamex_y".
    /// </summary>
    /// <see href="https://github.com/dotnet/roslyn/blob/aecd49800750d64e08767836e2678ffa62a4647f/src/Compilers/CSharp/Portable/Symbols/Synthesized/GeneratedNames.cs#L109" />
    private static readonly Regex FunctionNameRegex = new(@"^<(.*)>g__(?<name>[^\|]*)\|{0,1}[0-9]+(_[0-9]+)?$", RegexOptions.Compiled | RegexOptions.ExplicitCapture, matchTimeout: Timeout.InfiniteTimeSpan);
    private static readonly Regex LambdaContainingMethodNameRegex = new(@"^<(?<name>[^>]+)>b__[0-9]+(_[0-9]+)?$", RegexOptions.Compiled | RegexOptions.ExplicitCapture, matchTimeout: Timeout.InfiniteTimeSpan);

    private static readonly HashSet<string> TestAttributeNames = new(StringComparer.Ordinal)
    {
        "FactAttribute",
        "TheoryAttribute",
        "TestAttribute",
        "TestMethodAttribute",
    };

    /// <summary>Gets or sets the source root mappings used to resolve deterministic build paths.
    /// Keys are the mapped path prefixes (e.g. <c>/_/</c>, <c>/_1/</c>) and values are the
    /// corresponding real paths on disk. Populated automatically via a <c>[ModuleInitializer]</c>
    /// generated by the NuGet package's MSBuild props file.</summary>
    internal static Dictionary<string, string>? SourceRootMappings { get; set; }

    internal static void RegisterSourceRootMapping(string mappedPath, string realPath)
    {
        (SourceRootMappings ??= new Dictionary<string, string>(StringComparer.Ordinal))[mappedPath] = realPath;
    }

    /// <summary>Parses the MSBuild <c>$(PathMap)</c> property value and registers every entry.
    /// The format is a comma-separated list of <c>realPath=mappedPrefix</c> pairs, e.g.
    /// <c>/home/runner/work/Repo/=/_/,/home/runner/.nuget/packages/=/_1/,</c>.</summary>
    internal static void RegisterPathMap(string pathMap)
    {
        if (string.IsNullOrEmpty(pathMap))
            return;

        foreach (var segment in pathMap.Split(',', StringSplitOptions.RemoveEmptyEntries))
        {
            var eq = segment.IndexOf('=', StringComparison.Ordinal);
            if (eq <= 0)
                continue;

            var realPath = segment[..eq];
            var mappedPath = segment[(eq + 1)..];
            if (!string.IsNullOrEmpty(realPath) && !string.IsNullOrEmpty(mappedPath))
                RegisterSourceRootMapping(mappedPath, realPath);
        }
    }

    public static SnapshotCallerContext Create(string? filePath, int lineNumber, string? memberName)
    {
        var stackTrace = new StackTrace(fNeedFileInfo: true);
        string? discoveredMethodName = null;

        for (var i = 0; i < stackTrace.FrameCount; i++)
        {
            var frame = stackTrace.GetFrame(i);
            var method = frame?.GetMethod();
            if (method is null)
                continue;

            method = GetActualMethod(method);
            var declaringType = method.DeclaringType;
            if (declaringType?.Namespace?.StartsWith("Meziantou.Framework.SnapshotTesting", StringComparison.Ordinal) == true)
                continue;

            if (HasTestAttribute(method))
            {
                discoveredMethodName = NormalizeMethodName(method.Name);
                break;
            }

            discoveredMethodName ??= NormalizeMethodName(method.Name);
        }

        var sourceFilePath = filePath;
        if (sourceFilePath is null)
        {
            for (var i = 0; i < stackTrace.FrameCount; i++)
            {
                var frame = stackTrace.GetFrame(i);
                var candidateFilePath = frame?.GetFileName();
                if (candidateFilePath is not null)
                {
                    sourceFilePath = candidateFilePath;
                    break;
                }
            }
        }

        if (sourceFilePath is null)
            throw new SnapshotException("Cannot find the file to update from the call stack. The PDB may be missing.");

        discoveredMethodName ??= memberName ?? "Snapshot";
        return new SnapshotCallerContext(ResolveSourceFilePath(sourceFilePath), discoveredMethodName, memberName, lineNumber);
    }

    internal static FullPath ResolveSourceFilePath(string sourceFilePath)
    {
        return ResolveSourceFilePath(sourceFilePath, []);
    }

    internal static FullPath ResolveSourceFilePath(string sourceFilePath, IEnumerable<string?> sourceRoots)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(sourceFilePath);
        ArgumentNullException.ThrowIfNull(sourceRoots);

        var sourcePath = FullPath.FromPath(sourceFilePath);
        if (File.Exists(sourcePath))
            return sourcePath;

        // Use the original string (before FullPath normalization) for prefix matching so that
        // deterministic paths like /_1/foo are not mangled to drive-rooted paths on Windows.
        var relativePath = TryGetPathMappedRelativePath(sourceFilePath);
        if (relativePath is null)
            throw new SnapshotException($"Cannot find source file path '{sourceFilePath}'.");

        var normalizedRelativePath = relativePath.Replace('\\', Path.DirectorySeparatorChar).Replace('/', Path.DirectorySeparatorChar);

        // Try the registered source root mappings first (populated by the NuGet package's MSBuild props file)
        var mappings = SourceRootMappings;
        if (mappings is not null)
        {
            foreach (var realRoot in mappings.Values)
            {
                var candidatePath = FullPath.FromPath(realRoot) / normalizedRelativePath;
                if (File.Exists(candidatePath))
                    return candidatePath;
            }
        }

        foreach (var sourceRoot in sourceRoots)
        {
            if (string.IsNullOrWhiteSpace(sourceRoot))
                continue;

            var candidatePath = FullPath.FromPath(sourceRoot) / normalizedRelativePath;
            if (File.Exists(candidatePath))
                return candidatePath;
        }

        throw new SnapshotException($"Cannot find source file path '{sourceFilePath}'.");
    }

    private static string? TryGetPathMappedRelativePath(string path)
    {
        var normalizedPath = path.Replace('\\', '/');

        // Try registered mappings first — handles all prefixes including /_1/, /_2/, etc.
        var mappings = SourceRootMappings;
        if (mappings is not null)
        {
            foreach (var mappedPrefix in mappings.Keys)
            {
                if (normalizedPath.StartsWith(mappedPrefix, StringComparison.Ordinal))
                    return normalizedPath[mappedPrefix.Length..];
            }
        }

        // Generic fallbacks for the common /_/ pattern when no mappings are registered
        if (normalizedPath.StartsWith("/_/", StringComparison.Ordinal))
            return normalizedPath[3..];

        var root = Path.GetPathRoot(path);
        if (string.IsNullOrEmpty(root))
            return null;

        var remainder = path[root.Length..].Replace('\\', '/');
        if (remainder.StartsWith("_/", StringComparison.Ordinal))
            return remainder[2..];

        return null;
    }

    private static bool HasTestAttribute(MethodBase method)
    {
        IList<CustomAttributeData> attributes;
        try
        {
            attributes = method.GetCustomAttributesData();
        }
        catch (NotSupportedException)
        {
            return false;
        }
        catch (NotImplementedException)
        {
            return false;
        }

        foreach (var attribute in attributes)
        {
            var attributeName = attribute.AttributeType.Name;
            if (TestAttributeNames.Contains(attributeName))
            {
                return true;
            }
        }

        return false;
    }

    private static MethodBase GetActualMethod(MethodBase method)
    {
        if (method.DeclaringType is null || !method.DeclaringType.IsAssignableTo(typeof(IAsyncStateMachine)))
            return method;

        var parentType = method.DeclaringType.DeclaringType;
        if (parentType is null)
            return method;

        static MethodInfo[] GetDeclaredMethods(Type type) => type.GetMethods(BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static | BindingFlags.Instance | BindingFlags.DeclaredOnly);
        var methods = GetDeclaredMethods(parentType);
        if (methods is null)
            return method;

        foreach (var candidateMethod in methods)
        {
            var attributes = candidateMethod.GetCustomAttributes<StateMachineAttribute>(inherit: false);
            if (attributes is null)
                continue;

            foreach (var stateMachineAttribute in attributes)
            {
                if (stateMachineAttribute.StateMachineType == method.DeclaringType)
                    return candidateMethod;
            }
        }

        return method;
    }

    private static string NormalizeMethodName(string name)
    {
        if (TryGetLambdaContainingMethodName(name, out var lambdaContainingMethodName))
            return lambdaContainingMethodName;

        if (ParseLocalFunctionName(name, out var localFunctionName))
            return localFunctionName;

        return name;
    }

    private static bool TryGetLambdaContainingMethodName(string name, [NotNullWhen(true)] out string? containingMethodName)
    {
        containingMethodName = null;
        if (string.IsNullOrWhiteSpace(name))
            return false;

        var match = LambdaContainingMethodNameRegex.Match(name);
        if (!match.Success)
            return false;

        containingMethodName = match.Groups["name"].Value;
        return !string.IsNullOrEmpty(containingMethodName);
    }

    internal static bool ParseLocalFunctionName(string name, [NotNullWhen(true)] out string? functionName)
    {
        functionName = null;
        if (string.IsNullOrWhiteSpace(name))
            return false;

        var match = FunctionNameRegex.Match(name);
        functionName = match.Groups["name"].Value;
        return match.Success;
    }
}
