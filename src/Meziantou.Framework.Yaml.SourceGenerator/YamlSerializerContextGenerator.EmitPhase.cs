            builder.AppendLine();
            builder.AppendLine("        writer.WriteStartSequence();");
            builder.AppendLine("        for (var i = 0; i < value.Length; i++)");
            builder.AppendLine("        {");
            builder.AppendLine("            var element = value[i];");
