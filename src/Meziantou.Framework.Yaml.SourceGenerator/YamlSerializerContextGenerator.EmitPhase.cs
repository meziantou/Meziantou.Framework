            builder.AppendLine("        return;");
            builder.AppendLine("    }");
            return;
        }

        if (TryGetArrayElementType(typeSymbol, out var arrayElementType))
        {
            builder.AppendLine("        if (value is null)");
            builder.AppendLine("        {");
            builder.AppendLine("            writer.WriteNullValue();");
            builder.AppendLine("            return;");
            builder.AppendLine("        }");
            builder.AppendLine();
