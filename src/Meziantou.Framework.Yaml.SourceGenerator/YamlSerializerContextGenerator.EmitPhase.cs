    private static void EmitThrowIfNullForNonNullableMemberOnWrite(StringBuilder builder, MemberModel member, string declaringTypeName, string valueExpression, string indent)
    {
        if (!member.DisallowNullOnSerialize)
        {
            return;
        }

        AppendRespectNullableAnnotationsNullCheck(builder, indent, valueExpression);
        builder.Append(indent).AppendLine("{");
        builder.Append(indent).Append("    throw global::Meziantou.Framework.Yaml.Serialization.YamlThrowHelper.ThrowNullForNonNullableMember(typeof(")
            .Append(declaringTypeName)
            .Append("), ")
            .Append(member.SerializedNameExpressionForWrite)
            .AppendLine(");");
        builder.Append(indent).AppendLine("}");
    }

    private static void EmitThrowIfNullForNonNullableMemberOnRead(StringBuilder builder, MemberModel member, string valueExpression, string indent)
    {
        if (!member.DisallowNullOnDeserialize)
        {
            return;
        }

        var declaringType = member.Symbol.ContainingType ?? throw new InvalidOperationException("Member declaring type was not available.");
        var declaringTypeName = declaringType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        AppendRespectNullableAnnotationsNullCheck(builder, indent, valueExpression);
        builder.Append(indent).AppendLine("{");
        builder.Append(indent).Append("    throw global::Meziantou.Framework.Yaml.Serialization.YamlThrowHelper.ThrowNullForNonNullableMember(reader, typeof(")
            .Append(declaringTypeName)
            .Append("), ")
            .Append(member.SerializedNameExpressionForRead)
            .AppendLine(");");
        builder.Append(indent).AppendLine("}");
    }

    private static void EmitThrowIfNullForNonNullableConstructorParameter(StringBuilder builder, INamedTypeSymbol declaringType, IParameterSymbol parameter, string valueExpression, string indent)
    {
        if (!IsNonNullableReferenceType(parameter.Type))
        {
            return;
        }

        var declaringTypeName = declaringType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        AppendRespectNullableAnnotationsNullCheck(builder, indent, valueExpression);
        builder.Append(indent).AppendLine("{");
        builder.Append(indent).Append("    throw global::Meziantou.Framework.Yaml.Serialization.YamlThrowHelper.ThrowNullForNonNullableConstructorParameter(reader, typeof(")
