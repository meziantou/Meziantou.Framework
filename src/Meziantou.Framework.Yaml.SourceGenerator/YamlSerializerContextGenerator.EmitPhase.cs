    private static bool EmitContext(SourceProductionContext context, Compilation compilation, ContextModel model)
    {
        var validationResult = ValidateContext(compilation, model);
        if (validationResult.HasErrors)
        {
            return false;
        }

        var source = GenerateContextSource(model, validationResult.DerivedTypeMappings, validationResult.ResolvedTypes, validationResult.IndexByType);
        context.AddSource($"{model.TypeName}.g.cs", SourceText.From(source, Encoding.UTF8));
        return true;
    }

