        builder.Append("            if (rootAliasValue is ").Append(unionTypeName).AppendLine(" rootUnionValue)");
        builder.AppendLine("            {");
        builder.AppendLine("                return rootUnionValue;");
        builder.AppendLine("            }");
        builder.AppendLine();

        var writeCases = SortCSharpUnionCasesForWriting(unionCases);
        for (var i = 0; i < writeCases.Length; i++)
        {
            var unionCase = writeCases[i];
            var runtimeTypeName = unionCase.RuntimeType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
            builder.Append("            if (rootAliasValue is ").Append(runtimeTypeName).Append(" aliasCaseValue").Append(i).AppendLine(")");
            builder.AppendLine("            {");
            builder.Append("                return new ").Append(unionTypeName).Append("(aliasCaseValue").Append(i).AppendLine(");");
            builder.AppendLine("            }");
            builder.AppendLine();
        }

        builder.Append("            throw new global::Meziantou.Framework.Yaml.YamlException(reader.SourceName, reader.Start, reader.End, \"Union type '").Append(unionTypeName).AppendLine("' does not define a case that can represent the referenced value.\");");
        builder.AppendLine("        }");
        builder.AppendLine();

        builder.AppendLine("        if (reader.TokenType == global::Meziantou.Framework.Yaml.Serialization.YamlTokenType.Scalar && global::Meziantou.Framework.Yaml.Serialization.YamlScalar.IsNull(reader))");
        builder.AppendLine("        {");
        EmitReturnCSharpUnionNullValue(builder, unionTypeName, unionCases, "            ", consumeReader: true);
        builder.AppendLine("        }");
        builder.AppendLine();

        builder.AppendLine("        if (reader.TokenType == global::Meziantou.Framework.Yaml.Serialization.YamlTokenType.Scalar)");
        builder.AppendLine("        {");
        builder.AppendLine("            var scalarValue = global::Meziantou.Framework.Yaml.Serialization.YamlScalar.ResolveObject(reader);");
        builder.AppendLine("            if (scalarValue is bool)");
        builder.AppendLine("            {");
        EmitReadCSharpUnionKind(builder, unionTypeName, unionCases, CSharpUnionCaseKind.Boolean, indexByType, "                ");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            if (scalarValue is sbyte or byte or short or ushort or int or uint or long or ulong or nint or nuint or float or double or decimal or global::System.Half or global::System.Int128 or global::System.UInt128)");
        builder.AppendLine("            {");
        EmitReadCSharpUnionKind(builder, unionTypeName, unionCases, CSharpUnionCaseKind.Number, indexByType, "                ");
        builder.AppendLine("            }");
        builder.AppendLine();
        EmitReadCSharpUnionKind(builder, unionTypeName, unionCases, CSharpUnionCaseKind.String, indexByType, "            ");
        builder.AppendLine("        }");
        builder.AppendLine();

        builder.AppendLine("        if (reader.TokenType == global::Meziantou.Framework.Yaml.Serialization.YamlTokenType.StartSequence)");
        builder.AppendLine("        {");
        EmitReadCSharpUnionKind(builder, unionTypeName, unionCases, CSharpUnionCaseKind.Sequence, indexByType, "            ");
        builder.AppendLine("        }");
        builder.AppendLine();

        builder.AppendLine("        if (reader.TokenType == global::Meziantou.Framework.Yaml.Serialization.YamlTokenType.StartMapping)");
        builder.AppendLine("        {");
        EmitReadCSharpUnionKind(builder, unionTypeName, unionCases, CSharpUnionCaseKind.Mapping, indexByType, "            ");
        builder.AppendLine("        }");
        builder.AppendLine();

        builder.Append("        throw new global::Meziantou.Framework.Yaml.YamlException(reader.SourceName, reader.Start, reader.End, \"Token '\" + reader.TokenType + \"' cannot be deserialized into union type '").Append(unionTypeName).AppendLine("'.\");");
    }

    private static void EmitReturnCSharpUnionNullValue(
        StringBuilder builder,
        string unionTypeName,
        ImmutableArray<CSharpUnionCaseModel> unionCases,
        string indent,
        bool consumeReader)
    {
        var nullableCase = GetFirstNullableCSharpUnionCase(unionCases);
        if (nullableCase is null)
        {
            builder.Append(indent).Append("throw new global::Meziantou.Framework.Yaml.YamlException(reader.SourceName, reader.Start, reader.End, \"Union type '").Append(unionTypeName).AppendLine("' does not define a nullable case.\");");
            return;
        }

        if (consumeReader)
        {
            builder.Append(indent).AppendLine("reader.Read();");
        }

        var caseTypeName = nullableCase.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        builder.Append(indent).Append("return new ").Append(unionTypeName).Append("(default(").Append(caseTypeName).AppendLine("));");
    }

    private static void EmitReadCSharpUnionKind(
        StringBuilder builder,
        string unionTypeName,
        ImmutableArray<CSharpUnionCaseModel> unionCases,
        CSharpUnionCaseKind kind,
        Dictionary<ITypeSymbol, int> indexByType,
        string indent)
    {
        var unionCase = GetSingleCSharpUnionCase(unionCases, kind, out var ambiguous);
        if (ambiguous)
        {
            builder.Append(indent).Append("throw new global::Meziantou.Framework.Yaml.YamlException(reader.SourceName, reader.Start, reader.End, \"Cannot deserialize union type '").Append(unionTypeName).Append("' because multiple cases match YAML ").Append(GetCSharpUnionKindDescription(kind)).AppendLine(" values.\");");
            return;
        }

        if (unionCase is null)
        {
            builder.Append(indent).Append("throw new global::Meziantou.Framework.Yaml.YamlException(reader.SourceName, reader.Start, reader.End, \"Union type '").Append(unionTypeName).Append("' does not define a case that matches YAML ").Append(GetCSharpUnionKindDescription(kind)).AppendLine(" values.\");");
            return;
        }

        builder.Append(indent).AppendLine("{");
        EmitReadKnownType(builder, unionCase.Type, indexByType, "unionCaseValue", indent + "    ");
        builder.Append(indent).Append("    return new ").Append(unionTypeName).Append('(').Append(GetNonNullableValueExpression(unionCase.Type, "unionCaseValue")).AppendLine(");");
        builder.Append(indent).AppendLine("}");
    }

    private static void EmitWriteKnownType(StringBuilder builder, ITypeSymbol typeSymbol, Dictionary<ITypeSymbol, int> indexByType, string valueExpression, string indent)
    {
        var typeName = typeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        builder.Append(indent).Append("if (hasCustomConverters && writer.TryGetCustomConverter(typeof(").Append(typeName).AppendLine("), out var elementCustomConverter) && elementCustomConverter is not null)");
        builder.Append(indent).AppendLine("{");
        builder.Append(indent).Append("    elementCustomConverter.Write(writer, ").Append(valueExpression).AppendLine(");");
        builder.Append(indent).AppendLine("}");
        builder.Append(indent).AppendLine("else");
        builder.Append(indent).AppendLine("{");

        var innerIndent = indent + "    ";
        if (typeSymbol.SpecialType == SpecialType.System_Object)
        {
            builder.Append(innerIndent).AppendLine("var objectConverter = writer.GetConverter(typeof(object));");
            builder.Append(innerIndent).Append("objectConverter.Write(writer, ").Append(valueExpression).AppendLine(");");
            builder.Append(indent).AppendLine("}");
            return;
        }

        if (IsYamlNodeType(typeSymbol))
        {
            EmitWriteWithYamlNodeConverter(builder, typeName, valueExpression, innerIndent);
            builder.Append(indent).AppendLine("}");
            return;
        }

        if (TryEmitWriteScalar(builder, typeSymbol, valueExpression, innerIndent))
        {
            builder.Append(indent).AppendLine("}");
            return;
        }

        if (indexByType.TryGetValue(typeSymbol, out var typeIndex))
        {
            builder.Append(innerIndent).Append("WriteValue").Append(typeIndex).Append("(writer, ").Append(valueExpression).AppendLine(");");
            builder.Append(indent).AppendLine("}");
            return;
        }

        builder.Append(innerIndent).AppendLine("throw new global::System.NotSupportedException(\"The generated YAML serializer does not support this element type.\");");
        builder.Append(indent).AppendLine("}");
    }

    private static void EmitReadKnownType(StringBuilder builder, ITypeSymbol typeSymbol, Dictionary<ITypeSymbol, int> indexByType, string valueVarName, string indent)
    {
        var typeName = typeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        builder.Append(indent).Append("var ").Append(valueVarName).Append(" = default(").Append(typeName).AppendLine(");");
        builder.Append(indent).Append("if (hasCustomConverters && reader.TryGetCustomConverter(typeof(").Append(typeName).AppendLine("), out var elementCustomConverter) && elementCustomConverter is not null)");
        builder.Append(indent).AppendLine("{");
        builder.Append(indent).Append("    var untyped = elementCustomConverter.Read(reader, typeof(").Append(typeName).AppendLine("));");
        builder.Append(indent).AppendLine("    if (untyped is not null)");
        builder.Append(indent).AppendLine("    {");
        builder.Append(indent).Append("        ").Append(valueVarName).Append(" = (").Append(typeName).AppendLine(")untyped;");
        builder.Append(indent).AppendLine("    }");
        builder.Append(indent).AppendLine("}");
        builder.Append(indent).AppendLine("else");
        builder.Append(indent).AppendLine("{");

        var innerIndent = indent + "    ";
        if (typeSymbol.SpecialType == SpecialType.System_Object)
        {
            builder.Append(innerIndent).AppendLine("var objectConverter = reader.GetConverter(typeof(object));");
            builder.Append(innerIndent).AppendLine("var objectValue = objectConverter.Read(reader, typeof(object));");
            builder.Append(innerIndent).Append(valueVarName).AppendLine(" = objectValue;");
            builder.Append(indent).AppendLine("}");
            return;
        }

        if (IsYamlNodeType(typeSymbol))
        {
            EmitReadWithYamlNodeConverter(builder, typeName, innerIndent, valueExpression =>
            {
                builder.Append(innerIndent).Append(valueVarName).Append(" = ").Append(valueExpression).AppendLine(";");
            });
            builder.Append(indent).AppendLine("}");
            return;
        }

        if (IsKnownScalar(typeSymbol))
        {
            var scalarType = typeSymbol;
            var acceptsNull = !typeSymbol.IsValueType;
            if (typeSymbol is INamedTypeSymbol nullableType && nullableType.OriginalDefinition.SpecialType == SpecialType.System_Nullable_T)
            {
                scalarType = nullableType.TypeArguments[0];
                acceptsNull = true;
            }

            builder.Append(innerIndent).AppendLine("if (reader.TokenType != global::Meziantou.Framework.Yaml.Serialization.YamlTokenType.Scalar)");
            builder.Append(innerIndent).AppendLine("{");
            builder.Append(innerIndent).AppendLine("    throw global::Meziantou.Framework.Yaml.Serialization.YamlThrowHelper.ThrowExpectedScalar(reader);");
            builder.Append(innerIndent).AppendLine("}");
            if (acceptsNull)
            {
                builder.Append(innerIndent).AppendLine("if (global::Meziantou.Framework.Yaml.Serialization.YamlScalar.IsNull(reader))");
                builder.Append(innerIndent).AppendLine("{");
                builder.Append(innerIndent).AppendLine("    reader.Read();");
                builder.Append(innerIndent).AppendLine("}");
                builder.Append(innerIndent).AppendLine("else");
                builder.Append(innerIndent).AppendLine("{");
                EmitReadScalarAssignment(builder, scalarType, valueVarName, indent: innerIndent + "    ");
                builder.Append(innerIndent).AppendLine("    reader.Read();");
                builder.Append(innerIndent).AppendLine("}");
            }
            else
            {
                EmitReadScalarAssignment(builder, scalarType, valueVarName, indent: innerIndent);
                builder.Append(innerIndent).AppendLine("reader.Read();");
            }
            builder.Append(indent).AppendLine("}");
            return;
        }

        if (indexByType.TryGetValue(typeSymbol, out var typeIndex))
        {
            builder.Append(innerIndent).Append(valueVarName).Append(" = ReadValue").Append(typeIndex).AppendLine("(reader);");
            builder.Append(indent).AppendLine("}");
            return;
        }

        builder.Append(innerIndent).AppendLine("throw global::Meziantou.Framework.Yaml.Serialization.YamlThrowHelper.ThrowNotSupported(reader, \"The generated YAML serializer does not support this element type.\");");
        builder.Append(indent).AppendLine("}");
    }

    private static void EmitWriteWithYamlNodeConverter(StringBuilder builder, string typeName, string valueExpression, string indent)
    {
        builder.Append(indent).Append("var yamlNodeConverter = writer.GetConverter(typeof(").Append(typeName).AppendLine("));");
        builder.Append(indent).Append("yamlNodeConverter.Write(writer, ").Append(valueExpression).AppendLine(");");
    }

    private static void EmitReadWithYamlNodeConverter(StringBuilder builder, string typeName, string indent, Action<string> emitAssignment)
    {
        builder.Append(indent).Append("var yamlNodeConverter = reader.GetConverter(typeof(").Append(typeName).AppendLine("));");
        builder.Append(indent).Append("var yamlNodeValue = yamlNodeConverter.Read(reader, typeof(").Append(typeName).AppendLine("));");
        builder.Append(indent).AppendLine("if (yamlNodeValue is null)");
        builder.Append(indent).AppendLine("{");
        emitAssignment("default");
        builder.Append(indent).AppendLine("}");
        builder.Append(indent).AppendLine("else");
        builder.Append(indent).AppendLine("{");
        emitAssignment("(" + typeName + ")yamlNodeValue");
        builder.Append(indent).AppendLine("}");
    }

