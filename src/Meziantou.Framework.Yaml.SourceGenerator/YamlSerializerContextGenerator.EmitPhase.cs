            builder.AppendLine("                }");
            builder.AppendLine("                reader.Skip();");
        }
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        if (requiredMembers.Length != 0)
        {
            builder.AppendLine();
            builder.Append("        if (");
            for (var i = 0; i < requiredMembers.Length; i++)
            {
