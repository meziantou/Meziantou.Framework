            builder.AppendLine("            }");
            builder.AppendLine("        }");
        }

        builder.AppendLine();
        builder.AppendLine("        reader.Read();");
        builder.AppendLine("        return instance;");
    }

    private static void EmitDefaultsConstruction(
        StringBuilder builder,
        int index,
        string typeName,
        ImmutableArray<IParameterSymbol> parameters,
        string[] parameterValueVarNames,
        MemberModel[] requiredKeywordInitMembers,
        string indent)
    {
        builder.Append(indent).Append("__defaults").Append(index).Append(" = new ").Append(typeName).Append('(');
        for (var i = 0; i < parameters.Length; i++)
        {
            if (i != 0)
            {
                builder.Append(", ");
            }

            builder.Append(parameterValueVarNames[i]);
        }

        if (requiredKeywordInitMembers.Length != 0)
        {
            builder.AppendLine(")");
            builder.Append(indent).AppendLine("{");
            for (var i = 0; i < requiredKeywordInitMembers.Length; i++)
            {
                builder.Append(indent).Append("    ").Append(requiredKeywordInitMembers[i].Symbol.Name).AppendLine(" = default!,");
            }

            builder.Append(indent).AppendLine("};");
        }
        else
        {
            builder.AppendLine(");");
        }
    }

    private static void EmitReadValue(
        StringBuilder builder,
        int index,
        ITypeSymbol typeSymbol,
        string typeName,
        ImmutableArray<DerivedTypeMappingModel> derivedTypeMappings,
        Dictionary<ITypeSymbol, int> indexByType,
        YamlNamingPolicy? propertyNamingPolicy)
    {
        var attributeConverterTypeName = GetYamlConverterAttributeTypeName((ISymbol)typeSymbol);

        builder.Append("    private static ").Append(typeName).Append(typeSymbol.IsReferenceType ? "?" : string.Empty).Append(" ReadValue").Append(index)
            .AppendLine("(global::Meziantou.Framework.Yaml.Serialization.YamlReader reader)");
        builder.AppendLine("    {");
        builder.AppendLine("        var options = reader.Options;");
        builder.AppendLine("        var hasCustomConverters = options.Converters.Count != 0;");
        builder.AppendLine();

        if (attributeConverterTypeName is not null)
        {
            builder.Append("        global::Meziantou.Framework.Yaml.Serialization.YamlConverter attributeConverter = new ").Append(attributeConverterTypeName).AppendLine("();");
            builder.AppendLine("        if (attributeConverter is global::Meziantou.Framework.Yaml.Serialization.YamlConverterFactory factory)");
            builder.AppendLine("        {");
            builder.Append("            var created = factory.CreateConverter(typeof(").Append(typeName).AppendLine("), options);");
            builder.AppendLine("            if (created is null || !created.CanConvert(typeof(" + typeName + ")))");
            builder.AppendLine("            {");
            builder.AppendLine("                throw new global::System.InvalidOperationException(\"Converter factory returned an invalid converter.\");");
            builder.AppendLine("            }");
            builder.Append("            return (").Append(typeName).AppendLine(")created.Read(reader, typeof(" + typeName + "))!;");
            builder.AppendLine("        }");
            builder.Append("        return (").Append(typeName).AppendLine(")attributeConverter.Read(reader, typeof(" + typeName + "))!;");
            builder.AppendLine("    }");
            return;
        }

