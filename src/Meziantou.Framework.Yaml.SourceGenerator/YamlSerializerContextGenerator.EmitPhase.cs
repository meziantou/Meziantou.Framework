            builder.AppendLine();
        }
        builder.AppendLine("        reader.Read();");
        builder.AppendLine("        return instance;");
        builder.AppendLine("    }");
    }

    private static void EmitReadObjectCoreWithConstructor(
        StringBuilder builder,
        int index,
