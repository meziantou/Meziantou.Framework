    private static void EmitWriteCSharpUnionValue(
        StringBuilder builder,
        INamedTypeSymbol unionType,
        string unionTypeName,
        ImmutableArray<CSharpUnionCaseModel> unionCases,
        Dictionary<ITypeSymbol, int> indexByType)
    {
        if (unionType.IsReferenceType)
        {
            builder.AppendLine("        if (value is null)");
            builder.AppendLine("        {");
            builder.AppendLine("            writer.WriteNullValue();");
            builder.AppendLine("            return;");
            builder.AppendLine("        }");
            builder.AppendLine();
        }

        builder.AppendLine("        var unionValue = value.Value;");
        builder.AppendLine("        if (unionValue is null)");
        builder.AppendLine("        {");
        builder.AppendLine("            writer.WriteNullValue();");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();

        var writeCases = SortCSharpUnionCasesForWriting(unionCases);
        for (var i = 0; i < writeCases.Length; i++)
        {
            var unionCase = writeCases[i];
            var runtimeTypeName = unionCase.RuntimeType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
            builder.Append("        if (unionValue is ").Append(runtimeTypeName).Append(" unionCaseValue").Append(i).AppendLine(")");
            builder.AppendLine("        {");
