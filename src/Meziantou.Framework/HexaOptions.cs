namespace Meziantou.Framework;

/// <summary>
/// Specifies the casing options for hexadecimal string representation.
/// </summary>
public enum HexaOptions
{
    /// <summary>
    /// Use lowercase letters (a-f) for hexadecimal digits.
    /// </summary>
    LowerCase,

    /// <summary>
    /// Use uppercase letters (A-F) for hexadecimal digits.
    /// </summary>
    UpperCase,
}
