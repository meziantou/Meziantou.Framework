using Xunit.Sdk;

namespace Meziantou.Framework.PublicApiGenerator.MSBuild.Tests;

public sealed class PublicApiGeneratorMsBuildTests(PublicApiGeneratorMsBuildPackageFixture fixture) : IClassFixture<PublicApiGeneratorMsBuildPackageFixture>
{
    [Fact]
    public async Task GenerateOnBuild_SingleTarget_GeneratesFile()
    {
        await using var temporaryDirectory = TemporaryDirectory.Create();
        var projectDirectory = temporaryDirectory.CreateDirectory("single");
        CreateGlobalJson(projectDirectory, fixture.DotnetSdkVersion);
        CreateNuGetConfig(projectDirectory, fixture.PackagesDirectory);

        temporaryDirectory.CreateTextFile("single/Sample.csproj", $$"""
            <Project Sdk="Microsoft.NET.Sdk">
              <PropertyGroup>
                <TargetFramework>net8.0</TargetFramework>
                <PublicApiGeneratorOutputPath>PublicApi/PublicApi.g.cs</PublicApiGeneratorOutputPath>
              </PropertyGroup>

              <ItemGroup>
                <PackageReference Include="Meziantou.Framework.PublicApiGenerator.MSBuild" Version="{{fixture.PackageVersion}}" PrivateAssets="all" />
              </ItemGroup>
            </Project>
            """);
        temporaryDirectory.CreateTextFile("single/Sample.cs", """
            public class Sample
            {
                public void A()
                {
                }
            }
            """);

        await RunDotNetCommand(projectDirectory, ["restore", "--disable-build-servers"], expectedExitCode: 0);
        await RunDotNetCommand(projectDirectory, ["build", "--no-restore", "--disable-build-servers", "-nologo"], expectedExitCode: 0);

        var generatedFilePath = projectDirectory / "PublicApi" / "PublicApi.g.cs";
        Assert.True(File.Exists(generatedFilePath));
        var content = await File.ReadAllTextAsync(generatedFilePath, XunitCancellationToken);
        Assert.Contains("public class Sample", content, StringComparison.Ordinal);
        Assert.Contains("public void A() { }", content, StringComparison.Ordinal);
    }

    [Theory]
    [InlineData("SingleFile")]
    [InlineData("OneFilePerNamespace")]
    [InlineData("OneFilePerType")]
    public async Task GenerateOnBuild_SingleTarget_SupportsOutputModes(string fileLayout)
    {
        await using var temporaryDirectory = TemporaryDirectory.Create();
        var projectDirectory = temporaryDirectory.CreateDirectory("output-mode");
        CreateGlobalJson(projectDirectory, fixture.DotnetSdkVersion);
        CreateNuGetConfig(projectDirectory, fixture.PackagesDirectory);

        var outputPath = fileLayout is "SingleFile" ? "PublicApi/PublicApi.g.cs" : "PublicApi";
        temporaryDirectory.CreateTextFile("output-mode/Sample.csproj", $$"""
            <Project Sdk="Microsoft.NET.Sdk">
              <PropertyGroup>
                <TargetFramework>net8.0</TargetFramework>
                <PublicApiGeneratorOutputPath>{{outputPath}}</PublicApiGeneratorOutputPath>
                <PublicApiGeneratorFileLayout>{{fileLayout}}</PublicApiGeneratorFileLayout>
                <PublicApiGeneratorIncludeAutoGeneratedComment>false</PublicApiGeneratorIncludeAutoGeneratedComment>
              </PropertyGroup>

              <ItemGroup>
                <PackageReference Include="Meziantou.Framework.PublicApiGenerator.MSBuild" Version="{{fixture.PackageVersion}}" PrivateAssets="all" />
              </ItemGroup>
            </Project>
            """);
        temporaryDirectory.CreateTextFile("output-mode/Sample.cs", """
            public class GlobalType
            {
            }

            namespace Demo
            {
                public class Other
                {
                }

                public class Sample
                {
                }
            }
            """);

        await RunDotNetCommand(projectDirectory, ["restore", "--disable-build-servers"], expectedExitCode: 0);
        await RunDotNetCommand(projectDirectory, ["build", "--no-restore", "--disable-build-servers", "-nologo"], expectedExitCode: 0);

        switch (fileLayout)
        {
            case "SingleFile":
                {
                    var generatedFilePath = projectDirectory / "PublicApi" / "PublicApi.g.cs";
                    Assert.True(File.Exists(generatedFilePath));
                    var content = await File.ReadAllTextAsync(generatedFilePath, XunitCancellationToken);
                    Assert.Contains("public class GlobalType", content, StringComparison.Ordinal);
                    Assert.Contains("public class Other", content, StringComparison.Ordinal);
                    Assert.Contains("public class Sample", content, StringComparison.Ordinal);
                    break;
                }
            case "OneFilePerNamespace":
                Assert.True(File.Exists(projectDirectory / "PublicApi" / "Demo.g.cs"));
                Assert.True(File.Exists(projectDirectory / "PublicApi" / "GlobalNamespace.g.cs"));
                break;
            case "OneFilePerType":
                Assert.True(File.Exists(projectDirectory / "PublicApi" / "Demo.Other.g.cs"));
                Assert.True(File.Exists(projectDirectory / "PublicApi" / "Demo.Sample.g.cs"));
                Assert.True(File.Exists(projectDirectory / "PublicApi" / "GlobalType.g.cs"));
                break;
            default:
                throw new XunitException($"Unsupported file layout '{fileLayout}'.");
        }
    }

