            <Project Sdk="Microsoft.NET.Sdk">
              <PropertyGroup>
                <TargetFramework>net8.0</TargetFramework>
                <PublicApiGeneratorOutputPath>obj/PublicApi/PublicApi.g.cs</PublicApiGeneratorOutputPath>
                <PublicApiGeneratorIncludeAutoGeneratedComment>false</PublicApiGeneratorIncludeAutoGeneratedComment>
              </PropertyGroup>

              <ItemGroup>
                <PackageReference Include="Meziantou.Framework.PublicApiGenerator.MSBuild" Version="{{fixture.PackageVersion}}" PrivateAssets="all" />
              </ItemGroup>
            </Project>
            """);
        temporaryDirectory.CreateTextFile("validate-out-of-date/Sample.cs", """
            public class Sample
            {
                public void A()
                {
                }
            }
